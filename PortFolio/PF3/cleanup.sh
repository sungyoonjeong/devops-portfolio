#!/bin/bash
# ============================================================
# cleanup.sh
# 목적: 오래된 로그 파일 자동 삭제
# 실행: ./cleanup.sh
# 실행: ./cleanup.sh 14  (14일 이상 삭제, 기본값 7일)
# ============================================================

# ── 설정 ─────────────────────────────────────────────────────
LOG_DIR="."            # 로그 파일이 있는 디렉토리 (현재 폴더)
DAYS=${1:-7}           # 몇 일 이상 된 파일 삭제 (인수 없으면 기본 7일)
# ${1:-7}: $1(첫번째 인수)가 있으면 사용, 없으면 7 사용

TIMESTAMP=$(date "+%Y-%m-%d %H:%M:%S")

# ── 시작 로그 ─────────────────────────────────────────────────
echo "[$TIMESTAMP] 로그 정리 시작 (${DAYS}일 이상 파일 삭제)"

# ── 오래된 파일 찾기 ──────────────────────────────────────────
OLD_FILES=$(find "$LOG_DIR" -name "*.log" -mtime +"$DAYS" 2>/dev/null)
# find      : 파일 찾기
# -name "*.log" : .log 확장자 파일만
# -mtime +7 : 수정된 지 7일 초과한 파일
# 2>/dev/null: 에러 메시지 숨기기

# ── 파일 없으면 종료 ──────────────────────────────────────────
if [ -z "$OLD_FILES" ]; then
    # -z: 문자열이 비어있으면 true
    echo "[$TIMESTAMP] 삭제할 파일 없음"
    exit 0
fi

# ── 파일 삭제 ─────────────────────────────────────────────────
COUNT=0   # 삭제 파일 수 카운터

echo "$OLD_FILES" | while read FILE; do
    # while read: 한 줄씩 읽기
    if [ -f "$FILE" ]; then
        # -f: 파일이 존재하면
        echo "[$TIMESTAMP] 삭제: $FILE"
        rm "$FILE"
        COUNT=$((COUNT + 1))
    fi
done

# ── 완료 ─────────────────────────────────────────────────────
echo "[$TIMESTAMP] 정리 완료"