"""
=========================================
[문제 요약]
- 입력받은 정수 n의 모든 약수를 찾아서 그 합을 반환하는 함수 solution을 완성하기.
- 약수란? 어떤 수를 나누어떨어지게 하는 수 (예: 12의 약수는 1, 2, 3, 4, 6, 12).
- 제한 사항: n은 0 이상 3000 이하의 정수. (0일 때는 약수가 없으므로 합이 0이 되어야 함)

[알고리즘 구현 순서]
1. 약수들의 총합을 누적할 변수 answer를 0으로 초기화합니다.
2. 1부터 n까지의 모든 정수를 하나씩 순회합니다 (for i in range(1, n + 1)).
3. 만약 n을 i로 나누었을 때 나머지가 0이라면 (n % i == 0), i는 n의 약수입니다.
4. 약수로 판명된 i를 answer에 누적해서 더해줍니다.
5. 반복문이 모두 끝나면 최종 누적된 answer를 반환합니다.
=========================================
"""

def solution(n):
    answer = 0  # 약수의 합을 저장할 변수 초기화
    
    # 1부터 n까지 반복하며 약수를 찾습니다.
    # 만약 n이 0이면 range(1, 1)이 되어 루프를 돌지 않고 바로 answer(0)를 반환합니다.
    for i in range(1, n + 1):
        if n % i == 0:  # n이 i로 나누어떨어지면 i는 n의 약수입니다.
            answer += i  # 구한 약수를 answer에 더해줍니다.
            
    return answer  # 최종 약수의 합 반환


# =========================================
# [테스트 코드]
# =========================================
if __name__ == "__main__":
    print("--- 입출력 예시 테스트 ---")
    # 예시 1: 12의 약수 (1, 2, 3, 4, 6, 12) -> 합: 28
    print(f"입력: 12 | 출력: {solution(12)} | 정답 여부: {solution(12) == 28}")
    
    # 예시 2: 5의 약수 (1, 5) -> 합: 6
    print(f"입력: 5  | 출력: {solution(5)}  | 정답 여부: {solution(5) == 6}")
    
    print("\n--- 예외 케이스 테스트 ---")
    # 예외: n이 0일 때 -> 합: 0
    print(f"입력: 0  | 출력: {solution(0)}  | 정답 여부: {solution(0) == 0}")