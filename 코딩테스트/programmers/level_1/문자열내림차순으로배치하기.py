"""
[문제 요약]
- 문자열 s에 나타나는 문자를 큰 것부터 작은 순(내림차순)으로 정렬해 새로운 문자열을 리턴합니다.
- 대문자는 소문자보다 작은 것으로 간주합니다. (예: 'Z'는 'a'보다 작음)

[알고리즘 순서]
1. sorted() 함수에 reverse=True 옵션을 주어 문자열 s의 문자들을 내림차순으로 정렬합니다.
2. sorted()의 결과는 리스트 형태이므로, ''.join()을 사용하여 다시 하나의 문자열로 합쳐줍니다.
3. 합쳐진 문자열을 answer 변수에 저장한 뒤 반환합니다.
"""

def solution(s):
    # s를 내림차순 정렬한 리스트를 문자열로 합쳐서 answer에 저장
    # 파이썬은 기본적으로 대문자가 소문자보다 아스키코드 값이 작으므로, 내림차순 정렬 시 대문자가 뒤로 가게 됩니다.
    answer = ''.join(sorted(s, reverse=True))
    
    # 최종 결과 반환
    return answer

# --- 테스트 코드 ---
if __name__ == "__main__":
    print("테스트 케이스 1 (s='Zbcdefg', 기대값='gfedcbZ'):", solution("Zbcdefg"))