"""
상황:N*N 크기의 보드에 인형들이 쌓여있고, 크레인을 작동시켜 특정 열에서 가장 위에 있는 인형을 뽑아 우측 바구니에 쌓음
조건 :
    - 인형이 없는 열에서 크레인을 작동시키면 아무 일도 일어나지 않음
    - 바구니에 같은 모양의 인형 두개가 연속해서 쌓이면 두 인형은 터지면서 사라짐
출력 : 크레인 작동이 모두 끝난 후 터트러져 사라진 인형의 총 개수 구함

<알고리즘>
1) 바구니 자료구조 선택 : 인형은 아래서부터 위로 쌓이고, 가장 최근에 들어온 인형이 바로 이전 인형과 같은지 비교해야하므로 스택(stack)구조가 가장 적합
2) 크레인 위치 이동 : moves 배열을 돌면서 인형을 뽑을 열을 선택.문제의 인덱스는 1부터 시작하므로 코드에서는 col = move-1로 보정
3) 인형 탐색 : 위에서부터 아래로 내려가며 보드를 확인해야하므로 row를 0부터 len(board)-1까지 탐색하며 처음으로 0이 아닌 숫자를 찾음
4) 바구니에 넣기 및 터트리기
    - 인형을 찾으면 해당 칸 0(빈칸)으로 만듬
    - 바구니(스택)가 비어있지 않고, 바구니의 맨 위 인형(basket[-1])이 방금 뽑은 인형과 같다면 바구니에서 기존 인형을 꺼내고(pop), 사라진 인형 개수(answer)를 2개 추가함
    - 같지 않다면 바구니에 인형을 넣음(append)
    - 인형을 하나 뽑았으므로 해당 move에 대한 탐색을 종료(break) 다음크레인 작동
"""
def solution(board, moves):
    answer = 0 # 터트려져 사라진 인형의 총 개수를 저장할 변수입니다.
    basket = [] # 인형을 담을 바구니 역할을 할 스택(Stack) 리스트입니다.
    
    # 크레인의 움직임을 하나씩 순서대로 처리합니다.
    for move in moves:
        col = move - 1 # 격자의 인덱스는 0부터 시작하므로 move 값에서 1을 빼줍니다.
        
        # 격자의 가장 위(0번 행)부터 아래로 내려가며 인형이 있는지 확인합니다.
        for row in range(len(board)):
            if board[row][col] != 0: # 0이 아니라면 인형이 존재하는 것입니다.
                doll = board[row][col] # 뽑은 인형의 번호를 변수에 저장합니다.
                board[row][col] = 0 # 인형을 뽑았으므로 해당 격자 위치를 빈칸(0)으로 만듭니다.
                
                # 바구니에 인형이 있고, 맨 위의 인형이 방금 뽑은 인형과 같다면 터트립니다.
                if basket and basket[-1] == doll:
                    basket.pop() # 바구니 맨 위의 인형을 제거합니다.
                    answer += 2 # 두 개의 인형이 사라졌으므로 결과에 2를 더합니다.
                else:
                    basket.append(doll) # 위의 조건에 맞지 않으면 바구니에 인형을 쌓습니다.
                    
                break # 인형을 하나 뽑았으므로 이번 열(col)에서의 탐색을 중단하고 다음 move로 넘어갑니다.
                
    return answer # 최종적으로 터트려진 인형의 개수를 반환합니다.


