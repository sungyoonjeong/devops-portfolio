# 설명
# ① 앞부분: max(max(x) for x in sizes)
# for x in sizes: sizes 리스트에서 명함 한 장(x)을 차례대로 꺼냅니다. (예: x = [60, 50])

# max(x): 꺼낸 명함의 두 변 중 더 큰 값을 뽑아냅니다. ([60, 50] 중 60 선택)

# max(... for x in sizes): 모든 명함에서 각각 큰 값들을 모은 뒤, 그 값들 중 최종 최댓값을 구합니다.

# 💡 의미: 명함을 회전시켜 긴 쪽을 한 방향으로 몰았을 때 필요한 최소 가로 길이입니다.

# ② 뒷부분: max(min(x) for x in sizes)
# for x in sizes: 마찬가지로 명함 한 장(x)을 차례대로 꺼냅니다.

# min(x): 꺼낸 명함의 두 변 중 더 작은 값을 뽑아냅니다. ([60, 50] 중 50 선택)

# max(... for x in sizes): 모든 명함에서 각각 작은 값들을 모은 뒤, 그 값들 중 최종 최댓값을 구합니다.

# 💡 의미: 작은 값들 중에서도 가장 큰 명함은 수납해야 하므로, 지갑이 가져야 할 최소 세로 길이가 됩니다.

# 테스트 실행 함수
def test_solution():
    print("=== 최소직사각형 테스트 시작 ===")
    
    # 테스트 케이스 1 (프로그래머스 예제 1)
