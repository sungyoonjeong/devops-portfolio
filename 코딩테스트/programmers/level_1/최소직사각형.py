"""
지갑 크기 정하기
다양한 모양과 크기의 명함들을 모두 수납
작아서 들고 다니기 편해야함
ex) 가장긴 가로길이 80,가장긴 세로길이 70 인경우
80*70크기의 지갑을 만들면 모두 수납 가능
하지만 어떤 명함을 가로로 눕혀 수납하면 수납 가능한경우도 생각해야함

sizes : 모든 명함의 가로,세로 길이를 나타내는 2차원 배열
return : 지갑의 크기

<알고리즘>
1) 명함 방향 통일하기: 각 명함의 [가로, 세로] 크기 중에서 더 긴 길이를 가로(w), 더 짧은 길이를 세로(h)로 재정렬합니다. (명함을 필요하면 돌려놓는 과정)

2) 최댓값 찾기: * 정렬된 가로 길이들 중 가장 큰 값을 구합니다. (longest_w)
    - 정렬된 세로 길이들 중 가장 큰 값을 구합니다. (longest_h)

3) 지갑 크기 계산: 두 최댓값을 곱해줍니다
"""
def solution(sizes):
    answer = 0
    
    # 가로, 세로의 최대 길이를 저장할 변수 초기화
    longest_w = 0
    longest_h = 0
    
    # 모든 명함을 하나씩 순회
    for size in sizes:
        w = max(size)  # 명함의 두 변 중 더 긴 쪽을 w로 설정 (회전 고려)
        h = min(size)  # 명함의 두 변 중 더 짧은 쪽을 h로 설정 (회전 고려)
        
        # 현재 명함의 w가 기존 최댓값보다 크면 갱신
        if w > longest_w:
            longest_w = w
        # 현재 명함의 h가 기존 최댓값보다 크면 갱신
        if h > longest_h:
            longest_h = h
    
    # 가장 큰 가로와 세로를 곱해 최소 직사각형 지갑 크기 계산
    answer = longest_w * longest_h
    return answer


