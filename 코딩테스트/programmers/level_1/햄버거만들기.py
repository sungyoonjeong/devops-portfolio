"""
재료를 조리하면 조리 순서대로 아래서부터 위로 쌓임
정해진 순서(아래서부터, 빵-야채-고기-빵)로 쌓인 햄버거 포장.
재료 추가적으로 들어오는 일 없음
높이는 무시하여 재료가 높이 쌓여서 일이 힘들어지는 경우 없음

ex) 재료가 [야채,빵,빵,야채,고기,빵,야채,고기,빵]일때, 여섯번째 재료가 쌓였을때,
세 번째 재료부터 여섯번째 재료를 이용하여 햄버거를 포장하고, 아홉번째 재료가 쌓였을때, 
두번째 재료와 일곱번째 재료부터 아홉번째 재료를 이용하여 햄버거를 포장
즉, 2개의 햄버거를 포장

ingredient : 재료의 정보를 나타내는 정수 배열
return : 포장하는 햄버거의 개수
"""
"""
Step 1) 조리법대로 들어오는 재료를 하나씩 쌓아둘 빈 스택(리스트) stack을 생성합니다.
Step 2) ingredient 배열을 처음부터 끝까지 하나씩 순회하며 stack에 재료를 집어넣습니다(append).
Step 3) 재료를 넣을 때마다, 현재 stack의 맨 뒤에 있는 재료 4개가 완벽한 햄버거 순서인 [1, 2, 3, 1](빵-야채-고기-빵)을 이루고 있는지 검사합니다.
Step 4) 만약 뒤의 4개가 [1, 2, 3, 1]과 일치한다면:
    - 포장한 햄버거 개수(answer)를 1 증가시킵니다.
    - stack에서 방금 사용한 맨 뒤의 재료 4개를 제거(del 또는 pop)합니다.
Step 5) 모든 재료의 순회가 끝나면 최종 포장된 햄버거 개수를 반환합니다.
"""
def solution(ingredient):
    answer = 0       # 포장한 햄버거의 총 개수를 저장할 변수입니다.
    stack = []       # 재료를 순서대로 아래서부터 쌓아둘 스택(리스트)입니다.
    
    # 조리되어 들어오는 재료 배열을 처음부터 끝까지 하나씩 순회합니다.
    for ing in ingredient:
        stack.append(ing)  # 현재 들어온 재료를 스택의 맨 위에 쌓습니다.
        
        # 스택에 쌓인 재료가 최소 4개 이상이고, 맨 위의 재료 4개가 [1, 2, 3, 1] 순서인지 확인합니다.
        # stack[-4:]는 리스트의 가장 마지막 원소 4개를 잘라오는 슬라이싱 문법입니다.
        if len(stack) >= 4 and stack[-4:] == [1, 2, 3, 1]:
            answer += 1     # 완성된 햄버거를 1개 추가합니다.
            
            # 완성된 햄버거에 사용된 재료 4개를 스택에서 제거합니다.
            # del 문을 이용해 맨 뒤의 4개 요소를 한 번에 잘라냅니다.
            del stack[-4:]
            
    # 모든 재료 확인이 끝난 후 포장된 총 햄버거 개수를 반환합니다.
    return answer

