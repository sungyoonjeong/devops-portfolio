    print(f"입력값 〉 ingredient = {ingredient2}")
    print(f"기댓값 〉 {expected2}")
    print(f"실행결과 〉 {result2}")
    print(f"통과여부 〉 {'성공  (수정됨)' if result2 == expected2 else '실패 ❌'}")

    print("\n==========================================")