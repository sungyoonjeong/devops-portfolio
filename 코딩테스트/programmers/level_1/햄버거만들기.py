if __name__ == "__main__":
    print("=== [햄버거 만들기] 테스트 시작 ===")
    
    # 테스트 케이스 1
    ingredient1 = [2, 1, 1, 2, 3, 1, 2, 3, 1]
    expected1 = 2
    result1 = solution(ingredient1)
    print(f"\n[테스트 1]")
    print(f"입력값 〉 ingredient = {ingredient1}")
    print(f"기댓값 〉 {expected1}")
    print(f"실행결과 〉 {result1}")
    print(f"통과여부 〉 {'성공  (수정됨)' if result1 == expected1 else '실패 ❌'}")
    
    # 테스트 케이스 2
