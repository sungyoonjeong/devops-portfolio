"""
매일 1명의 가수가 노래를 부르고, 시청자들의 문자 투표수로 가수에게 점수 부여.
매일 출연한 가수의 점수가 지금까지 출연 가수들의 점수 중 상위 k번째 이내이면 
점수를 명예의 전당 목록에 올려 기념

즉, 프로그램 시작 이후 초기에 k일까지는 모든 출연 가수의 점수가 명예의 전당에 오름
k일 다음부터는 출연 가수의 점수가 기존 명예의 전당 목록의 k번째 순위의 가수 점수보다 높으면, 
출연 가수의 점수가 명예의 전당에 오르게 되고, 기존의 k번째 순위의 점수는 명예의 전당에서 내려옴

이 프로그램에서는 매일 "명예의 전당"의 최하위 점수를 발표
ex) k=3이고, 7일동안 진행된 가수의 점수가 [10,100,20,150,1,100,200]이라면
명예의 전당에서 발표된 점수는 [10,10,10,20,20,100,100]

k : 명예의 전당 목록의 점수의 개수
score : 1일부터 마지막 날까지 출연한 가수들의 점수
return : 매일 발표된 명예의 전당의 최하위 점수 
"""
"""
Step1) score를 순회하면서 range가 k 인 리스트에 높은 점수부터 저장.
Step2) 매번 저장하면서 리스트[k-1]을 answer에 append
"""
def solution(k, score):
    answer = []
    fame = []  # 명예의 전당 목록 (매일 갱신됨)
    
    for s in score:
        fame.append(s)     # 일단 오늘 가수 점수를 명예의 전당에 추가
        fame.sort()        # 오름차순으로 정렬 (가장 낮은 점수가 맨 앞으로 옴)
        
        # 명예의 전당 정원(k)을 넘어가면 가장 낮은 점수 제거
        if len(fame) > k:
            fame.pop(0)    # 맨 앞(가장 낮은 점수)을 탈락시킴
            
        # 현재 명예의 전당에서 가장 낮은 점수(맨 앞의 값)를 정답에 추가
        answer.append(fame[0])
        
    return answer

# --- 복사해서 바로 실행해보는 테스트 코드 ---
if __name__ == "__main__":
    print("테스트 1 결과:", "성공" if solution(3, [10, 100, 20, 150, 1, 100, 200]) == [10, 10, 10, 20, 20, 100, 100] else "실패")
    print("테스트 2 결과:", "성공" if solution(4, [0, 300, 40, 300, 20, 70, 150, 50, 500, 1000]) == [0, 0, 0, 0, 20, 40, 70, 70, 150, 300] else "실패")