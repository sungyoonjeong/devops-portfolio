"""
=========================================
[문제 요약]
- 입력된 문자열(s)은 공백(' ')으로 구분된 하나 이상의 단어로 구성됨.
- '단어 단위'로 인덱스를 계산하여, 짝수번째 알파벳은 대문자로, 홀수번째 알파벳은 소문자로 변환.
- 문자열 전체의 인덱스가 아니라 공백 기준 '단어별 인덱스'이며, 첫 글자는 0번째(짝수)로 처리.
- 주의: 연속된 공백이나 문자열 앞뒤의 공백이 그대로 유지되어야 함.

[알고리즘 구현 순서]
1. 결과를 누적할 빈 문자열 변수 answer를 준비합니다.
2. 각 단어 안에서 몇 번째 글자인지 추적할 인덱스 카운터 변수(idx)를 0으로 초기화합니다.
3. 전체 문자열 s를 한 글자씩(char) 순회합니다.
4. 만약 문자가 공백(' ')이라면:
   - 공백을 그대로 answer에 추가하고, 다음 단어를 대비해 idx를 다시 0으로 초기화합니다.
5. 만약 문자가 알파벳이라면:
   - idx가 짝수(idx % 2 == 0)일 때 대문자로 변환(.upper())하여 추가합니다.
   - idx가 홀수일 때 소문자로 변환(.lower())하여 추가합니다.
   - 변환 후 다음 글자를 위해 idx를 1 증가시킵니다.
6. 변환이 완료된 최종 answer 문자열을 반환합니다.
=========================================
"""

def solution(s):
    answer = ''          # 최종 변환된 문자열을 담을 변수 (기존 구조 유지)
    idx = 0              # 단어 내에서의 글자 인덱스를 나타낼 카운터 변수
    
    for char in s:       # 문자열 전체를 한 글자씩 순서대로 검사
        if char == ' ':  # 공백 문자를 만난 경우
            answer += ' '  # 결과에 공백을 그대로 더해줌 (연속 공백/앞뒤 공백 보존)
            idx = 0      # 공백을 만났으므로 다음 단어를 위해 인덱스를 0으로 초기화
        else:            # 알파벳을 만난 경우
            if idx % 2 == 0:
                answer += char.upper()  # 짝수번째 인덱스는 대문자로 변경 후 누적
            else:
                answer += char.lower()  # 홀수번째 인덱스는 소문자로 변경 후 누적
            idx += 1     # 단어 내 인덱스를 1 증가시킴
            
    return answer        # 최종 완성된 문자열 반환


