"""
두 문자열 X와 Y에서 공통으로 존재하는 숫자들을 찾습니다.

공통된 숫자들을 조합하여 만들 수 있는 가장 큰 정수를 문자열 형태로 반환해야 합니다.

공통된 숫자가 전혀 없다면 "-1", 공통된 숫자가 오직 0으로만 이루어져 있다면 "0"을 반환합니다.

<알고리즘>
Step 1) 공통으로 사용된 숫자들을 모아둘 빈 리스트 matched를 생성합니다.

Step 2) 가장 큰 수를 만들기 위해, 숫자의 큰 역순인 9부터 0까지 차례대로 순회합니다.

Step 3) 각 숫자가 문자열 X에 등장하는 횟수(X.count(i))와 Y에 등장하는 횟수(Y.count(i))를 각각 구합니다.

Step 4) 두 횟수 중 더 작은 값(최솟값)만큼 해당 숫자를 공통으로 짝지을 수 있습니다. 공통 개수만큼 숫자를 matched에 채워 넣습니다.

Step 5) 순회가 끝난 후 예외 처리를 진행합니다:

matched 리스트가 비어있다면 공통 숫자가 없는 것이므로 "-1"을 반환합니다.

리스트의 맨 앞 글자가 "0"이라면 전체가 0이라는 뜻이므로 "000..."이 되지 않도록 "0"을 반환합니다.

Step 6) 예외에 해당하지 않는다면 리스트를 하나의 문자열로 합쳐서 반환합니다.
"""
def solution(X, Y):
    matched = []  # X와 Y에서 공통으로 발견된 숫자들을 담을 리스트입니다.
    
    # 가장 큰 숫자를 만들어야 하므로 큰 숫자인 9부터 0까지 역순으로 확인합니다.
    for i in range(9, -1, -1):
        str_i = str(i)  # count 함수를 사용하기 위해 숫자를 문자열로 변환합니다.
        
        # 현재 숫자(str_i)가 X와 Y에 각각 몇 개씩 들어있는지 확인한 후, 더 적은 개수를 선택합니다.
        # 예: X에 '5'가 3개, Y에 '5'가 2개 있다면 공통으로 짝지을 수 있는 개수는 min(3, 2) = 2개입니다.
        count = min(X.count(str_i), Y.count(str_i))
        
        # 공통 개수(count)만큼 현재 숫자를 반복해서 matched 리스트에 추가합니다.
        matched.append(str_i * count)
        
    # 모든 숫자를 다 합친 최종 문자열을 만듭니다.
    answer = "".join(matched)
    
    # 예외 처리 1: 공통으로 나타나는 정수가 하나도 없다면 짝꿍이 없으므로 "-1"을 반환합니다.
    if answer == "":
        return "-1"
    
    # 예외 처리 2: 가장 큰 수부터 담았는데 맨 앞이 "0"이라는 것은 공통 숫자가 "0"뿐이라는 의미입니다.
    # "00"이나 "000" 대신 완벽한 정수 형태인 "0"을 반환합니다.
    if answer[0] == "0":
        return "0"
        
    # 모든 예외를 통과했다면 구한 가장 큰 정수 문자열을 반환합니다.
    return answer


