# 문제

# AI 엔지니어인 현식이는 데이터를 분석하는 작업을 진행하고 있습니다. 
# 데이터는 ["코드 번호(code)", "제조일(date)", "최대 수량(maximum)", "현재 수량(remain)"]으로 구성되어 
# 있으며 현식이는 이 데이터들 중 조건을 만족하는 데이터만 뽑아서 정렬하려 합니다.
# 예를 들어 다음과 같이 데이터가 주어진다면

# data = [[1, 20300104, 100, 80], [2, 20300804, 847, 37], [3, 20300401, 10, 8]]

# 이 데이터는 다음 표처럼 나타낼 수 있습니다.

# code	date	    maximum	    remain
# 1	    20300104	100	        80
# 2	    20300804	847	        37
# 3	    20300401	10	        8

# 주어진 데이터 중 "제조일이 20300501 이전인 물건들을 현재 수량이 적은 순서"로 정렬해야 한다면 
# 조건에 맞게 가공된 데이터는 다음과 같습니다.

# data = [[3,20300401,10,8],[1,20300104,100,80]]

# 리스트 data : 정렬한 데이터들이 담긴 이차원 정수 
# 문자열 ext : 어떤 정보를 기준으로 데이터를 뽑아낼지 의미 (date)
# 정수 val_ext : 뽑아낼 정보의 기준값 (20300501)
# 문자열 sort_by : 정보를 정렬할 기준이 되는 문자열 (remain)

# data에서 ext값이 val_ext보다 작은 데이터만 뽑은 후, sort_by에 해당하는 값을 기준으로 오름차순으로 
# 정렬하여 return하도록 solution 함수를 완성하여라. 

# 단, 조건을 만족하는 데이터는 항상 한 개 이상 존재합니다.

def solution(data, ext, val_ext, sort_by):
    # [방 하나짜리 임시 상자]
    # 프로그래머스가 처음에 만들어 둔 비어있는 리스트 상자입니다.
    # 나중에 아래에서 '진짜 데이터'로 싹 갈아치울(덮어쓸) 예정이라 그냥 둡니다.
    answer = [[]] 
    
    
    # ------------------------------------------------------------------------
    # [개념 1] 글자를 '방 번호(숫자)'로 바꾸는 자동 짝짓기 사전
    # ------------------------------------------------------------------------
    # 컴퓨터는 "date", "remain" 같은 글자만 보면 그게 데이터의 몇 번째 칸인지 모릅니다.
    # 그래서 "글자를 주면 -> 방 번호(숫자)를 알려주는" 사전을 우리가 직접 만듭니다.
    # "code"를 넣으면 0번 방, "date"를 넣으면 1번 방, "maximum"은 2번 방, "remain"은 3번 방입니다.
    col_idx = {"code": 0, "date": 1, "maximum": 2, "remain": 3}
    
    
    # ------------------------------------------------------------------------
    # [개념 2] 조건에 맞는 데이터만 쏙쏙 골라 담는 '한 줄 필터기'
    # ------------------------------------------------------------------------
    # 아래 코드는 오른쪽(for)부터 왼쪽(answer) 순서로 물 흐르듯 읽어야 가장 쉽습니다.
    #
    # 1. [for row in data]
    #    -> 큰 창고(data)에서 물건 상자(row)를 한 줄씩 차례대로 꺼냅니다.
    #    -> 예: 첫 번째 바퀴에는 row 변수에 [1, 20300104, 100, 80] 상자가 들어옵니다.
    #
    # 2. [col_idx[ext]]
    #    -> 만약 문제에서 ext 자리에 "date"라는 글자를 주었다면, 
    #    -> 위에서 만든 사전을 보고 "date"를 숫자 1로 바꿔치기합니다. (즉, 1번 방을 보라는 뜻!)
    #
    # 3. [if row[col_idx[ext]] < val_ext]  ===>  [if row[1] < val_ext]
    #    -> 방금 창고에서 꺼낸 상자(row)의 1번 방에 있는 값(날짜: 20300104)을 봅니다.
    #    -> 그 날짜가 정해준 기준 숫자(val_ext: 20300501)보다 '작은지' 확인합니다.
    #    -> 20300104는 20300501보다 작으므로, 이 상자는 합격(True)입니다!
    #
    # 4. [맨 앞의 row]
    #    -> 위 if 조건 검사에서 합격한 상자(row)만 국경을 통과시켜 줍니다.
    #
    # 5. [answer = [ ... ]]
    #    -> 합격해서 통과한 상자들만 모아서 '새로운 2차원 리스트 상자'를 만듭니다.
    #    -> 그리고 answer 변수에 그걸 집어넣어 버립니다.
    #    -> ⚠️ 이 순간, 처음에 들어있던 쓰레기 값인 [[]]는 완전히 지워지고 새 상자로 교체됩니다!
    answer = [row for row in data if row[col_idx[ext]] < val_ext]
    
    
    # ------------------------------------------------------------------------
    # [개념 3] 일회용 도우미(lambda)를 고용해서 순서대로 정렬하기
    # ------------------------------------------------------------------------
    # 컴퓨터에게 ".sort() (정렬해!)"라고만 말하면, 컴퓨터는 2차원 상자 안의 
    # 여러 숫자(번호, 날짜, 수량 등) 중 '무슨 숫자'를 기준으로 줄을 세워야 할지 모릅니다.
    # 그래서 key= 옵션에 "lambda"라는 이름 없는 일회용 도우미 함수를 고용해서 알려줍니다.
    #
    # 1. [col_idx[sort_by]]
    #    -> 만약 문제에서 sort_by 자리에 "remain"을 주었다면, 
    #    -> 사전을 보고 "remain"을 숫자 3으로 바꿉니다. (즉, 3번 방 숫자로 정렬하라는 뜻!)
    #
    # 2. [lambda x : x[3]]
    #    -> 도우미에게 규칙을 정해줍니다: "내가 상자를 하나씩 꺼내서 너한테 'x'라는 이름으로 줄게.
    #    -> 그럼 너는 그 상자의 3번 방에 들어있는 숫자(수량)를 쏙 뽑아서 나한테 다시 보여줘!"
    #
    # 3. [answer.sort(key=...)]
    #    -> 컴퓨터는 합격한 상자들을 하나씩 도우미에게 보여주며 3번 방 숫자들을 싹 걷어옵니다.
    #    -> 그리고 그 숫자가 '작은 상자부터 큰 상자 순서(오름차순)'로 상자들의 위치를 알맞게 재배치합니다.
    answer.sort(key=lambda x: x[col_idx[sort_by]])
    
    
    # 정리가 완벽하게 끝난 최종 상자를 프로그래머스에 제출합니다.
    return answer

# ================= [ 입출력 예시 기반 테스트 케이스 ] =================
if __name__ == "__main__":
    # 1. 시험용 원본 데이터 상자
    test_data = [
        [1, 20300104, 100, 80], 
        [2, 20300804, 847, 37], 
        [3, 20300401, 10, 8]
    ]
    
    # 2. 문제 조건: "date(1번방)"가 20300501보다 작은 것만 뽑아서, "remain(3번방)"이 작은 순서로 정렬해라!
    ext_param = "date"
    val_ext_param = 20300501
    sort_by_param = "remain"
    
    # 3. 정답 예측: 
    # 2번 상자는 날짜가 커서 탈락! 1번, 3번 상자만 통과함.
    # 통과한 1번(remain:80)과 3번(remain:8) 중 3번이 더 작으므로 3번 상자가 맨 앞으로 와야 함!
    expected = [
        [3, 20300401, 10, 8], 
        [1, 20300104, 100, 80]
    ]
    
    print("=== 코딩테스트 실행 결과 ===")
    result = solution(test_data, ext_param, val_ext_param, sort_by_param)
    
    print(f"내가 짠 코드의 결과: {result}")
    print(f"실제 문제의 기대 정답: {expected}")
    print(f"최종 채점 결과: {'정답입니다! 🎉' if result == expected else '틀렸습니다. ❌'}")