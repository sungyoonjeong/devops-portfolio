"""
=========================================
[문제 요약]
- 입력받은 문자열 s의 각 알파벳을 n만큼 밀어서 만든 암호문(시저 암호)을 반환하기.
- 예: "AB"를 1만큼 밀면 "BC", "z"를 1만큼 밀면 "a" (z 다음은 다시 a로 순환).
- 제한 사항: 
  - 공백(' ')은 아무리 밀어도 공백 상태를 유지해야 함.
  - n은 1 이상 25 이하의 자연수.

[알고리즘 구현 순서]
1. 결과를 누적할 빈 문자열 변수 answer를 준비합니다.
2. 문자열 s의 글자(char)들을 하나씩 순회합니다.
3. 만약 현재 글자가 공백(' ')이라면:
   - 계산 없이 그대로 answer에 공백을 더해줍니다.
4. 만약 현재 글자가 대문자(char.isupper())라면:
   - 'A'의 아스키코드를 기준으로 n만큼 밀어준 뒤, 알파벳 개수(26)로 나눈 나머지를 구해 다시 대문자 범위 내로 순환시킵니다.
5. 만약 현재 글자가 소문자(char.islower())라면:
   - 'a'의 아스키코드를 기준으로 n만큼 밀어준 뒤, 동일하게 26으로 나눈 나머지를 구해 소문자 범위 내로 순환시킵니다.
6. 변환된 글자들을 answer에 차곡차곡 누적한 후 최종 반환합니다.
=========================================
"""

def solution(s, n):
    answer = ''  # 최종 암호문을 담을 변수
    
    for char in s:
        if char == ' ':
            # 공백은 아무리 밀어도 공백이므로 그대로 유지
            answer += ' '
        elif char.isupper():
            # 대문자 암호화: 'A' 아스키코드(65) 기준으로 n만큼 밀고, 26 자리를 넘어가면 순환하도록 처리
            answer += chr((ord(char) - ord('A') + n) % 26 + ord('A'))
        else:
            # 소문자 암호화: 'a' 아스키코드(97) 기준으로 n만큼 밀고, 26 자리를 넘어가면 순환하도록 처리
            answer += chr((ord(char) - ord('a') + n) % 26 + ord('a'))
            
    return answer  # 완성된 시저 암호 문자열 반환


# =========================================
# [테스트 코드]
# =========================================
if __name__ == "__main__":
    print("--- 입출력 예시 테스트 ---")
    # 예시 1: "AB", 1 -> "BC"
    print(f"입력: s='AB', n=1     | 출력: '{solution('AB', 1)}' | 정답 여부: {solution('AB', 1) == 'BC'}")
    
    # 예시 2: "z", 1 -> "a" (순환 테스트)
    print(f"입력: s='z', n=1      | 출력: '{solution('z', 1)}' | 정답 여부: {solution('z', 1) == 'a'}")
    
    # 예시 3: "a B z", 4 -> "e F d" (대소문자 혼합 및 공백 포함)
    print(f"입력: s='a B z', n=4  | 출력: '{solution('a B z', 4)}' | 정답 여부: {solution('a B z', 4) == 'e F d'}")