"""
=========================================
[문제 요약]
- 숫자로 이루어진 문자열 s를 입력받아 이를 실제 정수(int)로 변환하여 반환하기.
- 제한 사항:
  - s의 길이는 1 이상 5 이하.
  - s의 맨 앞에는 부호(+, -)가 올 수 있음.
  - s는 부호와 숫자로만 이루어져 있으며, 잘못된 값이 입력되는 경우는 없음.
  - "0"으로 시작하지 않음.

[알고리즘 구현 순서]
1. 파이썬의 내장 함수인 int()를 활용합니다.
2. int() 함수는 문자열에 '+1234'나 '-1234'처럼 부호가 붙어 있어도 자동으로 올바른 정수형 데이터로 변환해 줍니다.
3. 변환된 정수 값을 그대로 반환합니다.
=========================================
"""

def solution(s):
    # 파이썬의 int() 함수는 부호(+, -)가 포함된 문자열도 알아서 숫자로 변환해 줍니다.
    answer = int(s)
    
    return answer  # 변환된 정수 값 반환


# =========================================
# [테스트 코드]
# =========================================
if __name__ == "__main__":
    print("--- 입출력 예시 테스트 ---")
    # 예시 1: 양수 문자열 변환 ("1234" -> 1234)
    print(f"입력: '1234'  | 출력: {solution('1234')}  | 정답 여부: {solution('1234') == 1234}")
    
    # 예시 2: 음수 문자열 변환 ("-1234" -> -1234)
    print(f"입력: '-1234' | 출력: {solution('-1234')} | 정답 여부: {solution('-1234') == -1234}")
    
    # 예시 3: 플러스 부호가 명시된 문자열 변환 ("+1234" -> 1234)
    print(f"입력: '+1234' | 출력: {solution('+1234')}  | 정답 여부: {solution('+1234') == 1234}")