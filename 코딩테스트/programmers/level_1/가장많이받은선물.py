# 문제 설명
# 선물을 직접 전하기 힘들 때 카카오톡 선물하기 기능을 이용해 축하 선물을 보낼 수 있습니다. 
# 당신의 친구들이 이번 달까지 선물을 주고받은 기록을 바탕으로 다음 달에 누가 선물을 많이 받을지 예측하려고 합니다.

# 두 사람이 선물을 주고받은 기록이 있다면, 이번 달까지 두 사람 사이에 더 많은 선물을 준 사람이 다음 달에 선물을 하나 받습니다.
# 예를 들어 A가 B에게 선물을 5번 줬고, B가 A에게 선물을 3번 줬다면 다음 달엔 A가 B에게 선물을 하나 받습니다.
# 두 사람이 선물을 주고받은 기록이 하나도 없거나 주고받은 수가 같다면, 선물 지수가 더 큰 사람이 선물 지수가 더 작은 사람에게 선물을 하나 받습니다.
# 선물 지수는 이번 달까지 자신이 친구들에게 준 선물의 수에서 받은 선물의 수를 뺀 값입니다.
# 예를 들어 A가 친구들에게 준 선물이 3개고 받은 선물이 10개라면 A의 선물 지수는 -7입니다. 
# B가 친구들에게 준 선물이 3개고 받은 선물이 2개라면 B의 선물 지수는 1입니다. 
# 만약 A와 B가 선물을 주고받은 적이 없거나 정확히 같은 수로 선물을 주고받았다면, 다음 달엔 B가 A에게 선물을 하나 받습니다.
# 만약 두 사람의 선물 지수도 같다면 다음 달에 선물을 주고받지 않습니다.
# 위에서 설명한 규칙대로 다음 달에 선물을 주고받을 때, 당신은 선물을 가장 많이 받을 친구가 받을 선물의 수를 알고 싶습니다.

# 친구들의 이름을 담은 1차원 문자열 배열 friends 이번 달까지 친구들이 주고받은 선물 기록을 담은 1차원 문자열 배열 gifts가 매개변수로 주어집니다. 
# 이때, 다음달에 가장 많은 선물을 받는 친구가 받을 선물의 수를 return 하도록 solution 함수를 완성해 주세요.

def solution(friends, gifts):
    # [함수 시작] 친구들의 이름 목록(friends)과 선물 기록 목록(gifts)을 받으며 시작합니다.

    # 1. 이름(문자열)을 배열의 방 번호(숫자) 인덱스로 빠르게 바꾸기 위한 사전을 만듭니다.
    # 예: {'muzi': 0, 'ryan': 1, 'frodo': 2, ...} 형태로 매칭해 줍니다.
    friend_idx = {name: i for i, name in enumerate(friends)}
    
    # 총 친구들이 몇 명인지 그 인원수를 구해서 n에 저장합니다.
    n = len(friends)
    
    # 친구들끼리 "서로" 선물을 몇 개씩 주고받았는지 기록할 2차원 표(행렬)를 만듭니다.
    # 가로 n칸, 세로 n칸 크기의 표를 만들고 모두 0으로 채워둡니다. [matrix[준사람][받은사람]]
    matrix = [[0] * n for _ in range(n)]
    
    # #friends:이름들이 총 몇 개의 선물을 주고받았는지 누적할 통장을 만듭니다.
    # 각 친구들이 준 선물의 총합 개수를 세어줄 방 n개를 만들고 0으로 채웁니다.
    give_count = [0] * n  
    
    # 각 친구들이 받은 선물의 총합 개수를 세어줄 방 n개를 만들고 0으로 채웁니다.
    take_count = [0] * n  
    
    # --------------------------------------------------------
    # [선물 기록 파악 단계] gifts 리스트에서 기록을 한 줄씩 꺼내 처리합니다.
    # --------------------------------------------------------
    for gift in gifts:
        # #gift 앞부분 준사람, 뒷부분 받은사람을 공백 기준으로 분리합니다.
        # 예: "muzi ryan" -> giver = "muzi", taker = "ryan"
        giver, taker = gift.split()
        
        # 문자로 된 이름을 미리 만들어둔 사전을 통해 숫자로 된 방 번호(인덱스)로 바꿉니다.
        g_idx = friend_idx[giver] # 준 사람의 방 번호 (예: 0)
        t_idx = friend_idx[taker] # 받은 사람의 방 번호 (예: 1)
        
        # #앞부분만 떼서 준선물 갯수파악 (준 사람 가방에 +1)
        give_count[g_idx] += 1
        
        # #뒷부분만 떼서 받은선물 갯수 파악 (받은 사람 가방에 +1)
        take_count[t_idx] += 1
        
        # 나중에 둘이서 1:1로 비교할 때 써먹기 위해 세부 장부 표(matrix)에도 똑같이 기록합니다.
        # matrix[준사람][받은사람] 칸의 숫자를 1 늘려줍니다.
        matrix[g_idx][t_idx] += 1

    # --------------------------------------------------------
    # [선물지수 계산 단계]
    # --------------------------------------------------------
    # #선물지수는 준선물 - 받은선물 공식으로 모든 친구의 선물 지수를 구합니다.
    # i번 친구의 (총 준 개수 - 총 받은 개수)를 계산하여 리스트로 쪼르륵 저장합니다.
    gift_indices = [give_count[i] - take_count[i] for i in range(n)]

    # --------------------------------------------------------
    # [다음 달 선물 예측 및 1:1 대조 단계]
    # --------------------------------------------------------
    # 다음 달에 각자 선물을 몇 개씩 받게 될지 결과를 누적할 방 n개를 만들고 0으로 채웁니다.
    next_month_gifts = [0] * n  
    
    # 모든 친구들을 중복 없이 1:1로 한 쌍씩 매칭하기 위해 2중 for문을 돌립니다.
    for i in range(n):
        # j는 i + 1부터 시작하여 이미 비교한 쌍(i와 j)을 다시 비교하지 않도록 방지합니다.
        for j in range(i + 1, n):
            
            # i번 친구가 j번 친구에게 준 선물 개수를 2차원 장부에서 가져옵니다.
            i_to_j = matrix[i][j]  
            
            # j번 친구가 i번 친구에게 준 선물 개수를 2차원 장부에서 가져옵니다.
            j_to_i = matrix[j][i]  
            
            # [규칙 1] 두 사람이 선물을 주고받은 기록이 있고, 준 개수가 서로 다를 때
            if i_to_j > j_to_i:
                # i가 j에게 선물을 더 많이 줬으므로, i의 다음 달 선물 점수를 1 올립니다.
                next_month_gifts[i] += 1  
            elif i_to_j < j_to_i:
                # j가 i에게 선물을 더 많이 줬으므로, j의 다음 달 선물 점수를 1 올립니다.
                next_month_gifts[j] += 1  
                
            # [규칙 2] 두 사람이 선물을 주고받은 적이 없거나, 주고받은 개수가 정확히 똑같을 때
            else:
                # 미리 구해둔 #선물지수(준선물 - 받은선물)를 꺼내서 비교합니다.
                if gift_indices[i] > gift_indices[j]:
                    # i의 선물 지수가 더 크므로, i가 다음 달 선물을 1개 받습니다.
                    next_month_gifts[i] += 1  
                elif gift_indices[i] < gift_indices[j]:
                    # j의 선물 지수가 더 크므로, j가 다음 달 선물을 1개 받습니다.
                    next_month_gifts[j] += 1  
                # 만약 선물 지수까지 완벽히 같다면 규칙에 따라 아무도 선물을 주고받지 않고 패스합니다.

    # --------------------------------------------------------
    # [최종 결과 반환]
    # --------------------------------------------------------
    # 예측한 다음 달 선물 가방(next_month_gifts) 중에서 가장 숫자가 큰 값(최댓값)을 찾아 반환합니다.
    return max(next_month_gifts)



# 위쪽에 solution 함수 코드가 작성되어 있어야 합니다.

# 제공해주신 3가지 공식 테스트 케이스 데이터 선언
test_cases = [
    {
        "friends": ["muzi", "ryan", "frodo", "neo"],
        "gifts": ["muzi frodo", "muzi frodo", "ryan muzi", "ryan muzi", "ryan muzi", "frodo muzi", "frodo ryan", "neo muzi"],
        "expected": 2
    },
    {
        "friends": ["joy", "brad", "alessandro", "conan", "david"],
        "gifts": ["alessandro brad", "alessandro joy", "alessandro conan", "david alessandro", "alessandro david"],
        "expected": 4
    },
    {
        "friends": ["a", "b", "c"],
        "gifts": ["a b", "b a", "c a", "a c", "a c", "c a"],
        "expected": 0
    }
]

# 반복문을 통해 순서대로 검증 진행
for i, tc in enumerate(test_cases, 1):
    # 구현한 solution 함수 호출
    result = solution(tc["friends"], tc["gifts"])
    
    print(f"--- 테스트 케이스 {i} ---")
    print(f"참여 친구들: {tc['friends']}")
    print(f"선물 내역 총 개수: {len(tc['gifts'])}개")
    print(f"실행 결과 (내 코드): {result}")
    print(f"기대 정답 (표의 값): {tc['expected']}")
    
    # 정답과 일치하는지 비교
    if result == tc["expected"]:
        print("결과:        ✅ 통과 (PASS)")
    else:
        print("결과:        ❌ 실패 (FAIL)")
    print()
