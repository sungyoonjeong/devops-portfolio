# 길이 = n미터 벽

# 페인트 칠
# 1) 벽을 1미터 길이의 구역 n개로 나눔.
# 2) 각 구역에 왼쪽부터 순서대로 1~n까지 번호 붙임
# 3) 페인트를 다시 칠해야 할 구역들 정함

# 페인트 칠하는 롤러의 길이 = m 미터

# 롤러 페인트 한번 칠하는 규칙
#     1) 롤러가 벽에서 벗어나면 안됨
#     2) 구역의 일부분만 포함되도록 칠하면 안됨

# 정수 n: 각 구역 번호
# 정수 m: 롤러의 길이
# section: 다시 페인트를 칠하기로 정한 구역들의 번호가 담긴 정수 배열

# 위 3개를 매개변수로 
# 롤러로 페인트 칠해야하는 최소 횟수 return 하는 solution
'''
step1) 핵심 아이디어 파악하기(greedy)
    -최소한으로 롤러를 칠하려면, 아직 페인트가 칠해지지 않은 가장 첫번째 구역을 만났을대 바로 시작
    -롤러를 더 왼쪽에서 시작하면 뒤쪽영역을 그만큼 덜 칠하게 되므로 손해

step2) 롤러의 커버 범위 계산
    - 롤러의 길이가 m일때, 어떤 구역 x번부터 롤러를 대고 오른쪽으로 쭉 밀면 x번부터 x+m-1번 구역까지 한번에 칠해짐

step3) 변수설정
    - paint_count: 페인트를 칠한 횟수를 기록함(0으로 초기화)
    - current_coverage: 현재까지 롤러로 칠해서 완료된 우측 끝 구역 번호를 기록(초기에는 아무것도 안칠했으므로 0으로 초기화)
    - 페인트를 칠해야하는 구역(section)을 하나씩 순서대로 확인하면서, 현재 구역이 이미 이전에 칠해진 범위(current_coverage) 안에 있다면 그냥 넘어가고, 범위를 벗어났다면 새롭게 롤러를 한번 더 사용
'''
def solution(n, m, section):
    paint_count = 0      # 롤러로 페인트를 칠한 총 횟수를 저장할 변수입니다.
    current_coverage = 0 # 현재까지의 롤러질로 페인트가 완료된 마지막 구역의 번호입니다.
    
    # 다시 칠해야 하는 구역들을 하나씩 순서대로 확인합니다.
    for target in section:
        # 현재 확인 중인 구역(target)이 이미 이전에 칠한 범위보다 뒤에 있다면 (아직 안 칠해졌다면)
        if target > current_coverage:
            paint_count += 1 # 롤러를 새로 대고 한 번 칠합니다.
            
            # 현재 구역(target)부터 롤러의 길이(m)만큼 칠해지므로, 
            # 이번 롤러질로 커버되는 마지막 구역 번호는 'target + m - 1'이 됩니다.
            current_coverage = target + m - 1
            
    # 모든 구역을 확인한 후 롤러를 사용한 최소 횟수를 반환합니다.
    return paint_count


# for target in section을 하면 section에 들어온 배열이 만약 [2,3,6]이라할때, 2,3,6을 하나씩 뺌
# for i in range(len((section))을하면 i에는 0~2까지 들어감

# ========================================================
# 🧪 입출력 예를 활용한 테스트케이스 실행 구문
# ========================================================

# 입출력 예 1번 (화면에 나온 n=8, m=4, section=[2,3,6] 케이스)
print("--- 테스트케이스 1 ---")
result1 = solution(8, 4, [2, 3, 6])
print(f"계산된 결과: {result1} / 기댓값: 2")
print(f"정답 여부: {result1 == 2}\n")

# 입출력 예 2번 (화면에 나온 n=5, m=4, section=[1,3] 케이스)
print("--- 테스트케이스 2 ---")
result2 = solution(5, 4, [1, 3])
print(f"계산된 결과: {result2} / 기댓값: 1")
print(f"정답 여부: {result2 == 1}\n")

# 입출력 예 3번 (화면에 나온 n=4, m=1, section=[1,2,3,4] 케이스)
print("--- 테스트케이스 3 ---")
result3 = solution(4, 1, [1, 2, 3, 4])
print(f"계산된 결과: {result3} / 기댓값: 4")
print(f"정답 여부: {result3 == 4}\n")