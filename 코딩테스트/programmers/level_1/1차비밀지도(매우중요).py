"""
[1차] 비밀지도 - 문제 요약
1. 목표: 두 개의 정수 배열(arr1, arr2)을 이진수로 변환하고 비트 논리합(OR) 연산을 하여 하나의 지도로 합칩니다.
2. 벽과 공백의 표현: 두 지도 중 어느 하나라도 '1'(벽)인 부분은 전체 지도에서도 '#'가 되고, 둘 다 '0'(공백)인 부분은 ' '가 됩니다.
3. 자릿수 맞추기: 이진수로 변환했을 때 전체 지도의 크기 n보다 자릿수가 부족하다면 앞부분을 '0'으로 채워야(Padding) 합니다.
4. 출력: 원래의 비밀지도를 해독한 문자열 배열을 반환합니다.


알고리즘 해결 순서 (Step-by-Step)
1. 결과 배열 준비: 변환된 문자열 지도를 담을 빈 리스트(answer)를 준비합니다.
2. 두 배열 동시 순회: zip() 함수를 활용하여 arr1과 arr2의 같은 인덱스 값을 동시에 꺼내어 반복문을 돌립니다.
3. 비트 OR 연산 (|): 두 지도의 벽 위치를 하나로 합치기 위해 비트 논리합(|) 연산을 수행합니다. (예: 9 | 30 = 31)
4. 이진수 변환: 연산 결과를 bin() 함수를 사용해 이진수 문자열로 바꾼 뒤, 앞의 이진수 식별자('0b')를 잘라냅니다.
5. 자릿수 채우기 (rjust): n 칸의 크기를 맞추기 위해, 변환된 이진수 문자열의 길이가 n보다 작다면 왼쪽에 '0'을 채워 넣습니다.
6. 벽(#)과 공백( ) 치환: 이진수 문자열에서 '1'은 '#'으로, '0'은 ' '으로 바꾸어(replace) 최종 지도의 한 줄을 만듭니다.
7. 결과 저장 및 반환: 변환된 문자열을 answer 리스트에 추가하고, 반복문이 종료되면 answer를 반환합니다.


핵심 함수 및 문법 자세한 설명
1. zip(arr1, arr2)
   - 역할: 여러 개의 반복 가능한(Iterable) 객체를 받아, 동일한 인덱스의 요소끼리 튜플로 묶어주는 함수입니다.
   - 예시: zip([9, 20], [30, 1]) -> 첫 번째 루프에서는 (9, 30), 두 번째 루프에서는 (20, 1)을 차례대로 반환합니다.
   - 장점: 인덱스 번호(i)로 배열에 직접 접근(arr1[i])할 필요 없이, 여러 배열의 같은 위치 값을 안전하고 직관적으로 꺼낼 수 있습니다.

2. bin(숫자)
   - 역할: 10진수 정수를 이진수(Binary) 문자열로 변환해 주는 파이썬 내장 함수입니다.
   - 특징: 변환된 문자열의 앞부분에는 이진수임을 나타내는 식별자 '0b'가 접두어로 항상 붙습니다. (예: bin(9) -> '0b1001')
   - 팁: 이 문제에서는 순수한 이진수 숫자만 필요하므로 슬라이싱 문법인 `[2:]`를 붙여 앞의 '0b' 두 글자를 잘라내고 사용합니다.

3. 문자열.rjust(길이, 채울문자)
   - 역할: Right Justify의 약자로, 문자열을 오른쪽으로 정렬하고 지정된 '길이'에 못 미치는 만큼 왼쪽의 빈 공간을 '채울문자'로 채웁니다.
   - 필요성: 이진수 변환 시 앞자리가 0이면 생략되어 문자열 길이가 n보다 짧아집니다. (예: n=5일 때 1은 '1'이 됨)
   - 예시: "1001".rjust(5, "0") -> 총 5자리를 맞추기 위해 왼쪽에 '0'을 하나 채워 "01001"을 만듭니다.

4. 문자열.replace(찾을문자, 바꿀문자)
   - 역할: 문자열 안에서 특정 문자를 찾아 다른 문자로 모두 변경(치환)해 주는 메서드입니다.
   - 특징: 원본 문자열을 직접 수정하지 않고 변경된 새로운 문자열을 반환하므로, 연속해서 사용(메서드 체이닝)할 수 있습니다.
   - 예시: "01".replace("1", "#").replace("0", " ") -> '1'이 '#'이 되어 "0#"이 되고, 이어서 '0'이 ' '이 되어 최종적으로 " #"가 됩니다.
"""


def solution(n, arr1, arr2):
    # 1. 해독된 비밀지도의 가로줄들을 순서대로 담을 리스트를 선언합니다.
    answer = []

    # 2. zip 함수를 이용해 지도 1(arr1)과 지도 2(arr2)의 같은 행에 있는 숫자를 동시에 하나씩 꺼냅니다.
    for num1, num2 in zip(arr1, arr2):

        # 3. 비트 OR(|) 연산으로 두 지도를 겹치고, bin()으로 이진수 문자열로 바꾼 뒤 앞의 '0b'를 슬라이싱([2:])으로 제외합니다.
        binary_str = bin(num1 | num2)[2:]

        # 4. rjust(n, '0')를 사용해 이진수 문자열의 길이가 n이 되도록 왼쪽에 부족한 만큼 '0'을 채워 넣습니다.
        padded_str = binary_str.rjust(n, "0")

        # 5. 문자열 내의 모든 '1'은 벽('#')으로, 모든 '0'은 공백(' ')으로 치환하여 지도 한 줄을 완성합니다.
        line = padded_str.replace("1", "#").replace("0", " ")

        # 6. 완성된 가로줄 한 행을 최종 결과 리스트에 하나씩 추가합니다.
        answer.append(line)

    # 7. 원래의 비밀지도로 해독이 완료된 문자열 배열을 반환합니다.
    return answer


