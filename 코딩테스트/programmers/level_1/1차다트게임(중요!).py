"""
1. 문제 요약
    - 입력: 점수(0~10), 보너스(S, D, T), 옵션(*, #)이 섞인 3세트의 문자열 (예: 1S2D*3T)
    - 규칙:
        - 보너스: S($^{1}$제곱), D($^{2}$제곱), T($^{3}$제곱)로 해당 점수를 계산합니다.
        - 스타상(*): 현재 점수와 바로 직전 점수를 각각 2배로 만듭니다. 
        (첫 번째 기회에 나오면 현재 점수만 2배, 중첩되면 4배)
        - 아차상(#): 현재 점수를 마이너스로 만듭니다. 
        (스타상과 중첩되면 -2배)출력: 3번의 기회로 계산된 최종 총점의 합계를 반환합니다.
        
2. 알고리즘
1) 점수 저장소 준비: 각 기회(총 3번)의 점수를 저장하고 연산할 빈 리스트(scores)를 생성합니다.
2) 문자열 한 글자씩 순회: 입력받은 문자열을 for문으로 하나씩 검사합니다.
3) 글자 종류별 조건 분기:
    - 숫자일 때: 단, 점수가 10인 경우 글자가 '1'과 '0'으로 쪼개져 들어오므로, 직전 글자가 '1'이고 현재 글자가 '0'인 특수 예외를 처리하여 10을 점수 리스트에 넣습니다. 일반 숫자는 정수로 변환하여 리스트에 추가합니다.
    - 보너스(S, D, T)일 때: 리스트의 가장 최근 점수(scores[-1])를 꺼내 각각 1제곱, 2제곱, 3제곱을 해준 뒤 다시 업데이트합니다.
    - 옵션(*, #)일 때:
        - *이면 현재 점수(scores[-1])를 2배로 만들고, 직전 점수(만약 존재한다면 scores[-2])도 2배로 만듭니다.
        - #이면 현재 점수(scores[-1])에 -1을 곱합니다.
4) 결과 반환: 최종적으로 scores 리스트에 쌓인 3개의 점수를 모두 더해(sum(scores)) 반환합니다.
"""
def solution(dartResult):
    # 1. 각 차례의 점수를 저장하여 연산할 리스트를 선언합니다.
    scores = []

    # 2. '10'점처럼 두 글자 이상인 숫자를 온전히 모으기 위한 임시 문자열 변수입니다.
    score_str = ""

    # 3. 다트 결과 문자열을 한 글자씩 순회하며 조건을 파악합니다.
    for char in dartResult:

        # 4. 현재 문자가 숫자라면, 임시 문자열 변수에 차례대로 붙여줍니다.
        if char.isdigit():
            score_str += char

        # 5. 보너스 문자를 만나면 숫자가 끝난 것이므로, 제곱 연산 후 점수 리스트에 추가합니다.
        elif char == "S":
            scores.append(int(score_str) ** 1)  # Single: 현재까지 모인 점수의 1제곱을 구합니다.
            score_str = ""  # 다음 점수를 새로 받기 위해 임시 변수를 비워줍니다.
        elif char == "D":
            scores.append(int(score_str) ** 2)  # Double: 현재까지 모인 점수의 2제곱을 구합니다.
            score_str = ""  # 다음 점수를 새로 받기 위해 임시 변수를 비워줍니다.
        elif char == "T":
            scores.append(int(score_str) ** 3)  # Triple: 현재까지 모인 점수의 3제곱을 구합니다.
            score_str = ""  # 다음 점수를 새로 받기 위해 임시 변수를 비워줍니다.

        # 6. 옵션 문자를 만나면 리스트에 방금 들어간 점수들을 규칙에 맞게 수정합니다.
        elif char == "*":
            scores[-1] *= 2  # 스타상은 방금 계산된 현재 점수를 2배로 만듭니다.
            if len(scores) >= 2:
                scores[-2] *= 2  # 만약 이전 차례의 점수가 존재한다면, 그 점수도 함께 2배로 만듭니다.
        elif char == "#":
            scores[-1] *= -1  # 아차상은 방금 계산된 현재 점수에 -1을 곱해 마이너스로 만듭니다.

    # 7. 최종적으로 리스트에 기록된 세 차례 점수의 총합을 반환합니다.
    return sum(scores)


