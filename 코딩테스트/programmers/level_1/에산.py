"""
최대한 많은 부서의 물품 구매하기
각 부서가 신청한 금액만큼을모두 지원해줘야함
ex) 1000원을 신청한 부서에는 정확히 1000원을 지원해야함. 1000원보다 적은 금액 지원 못함

d : 부서별로 신청한 금액이 들어있는 배열
budget : 예산
return : 최대 몇 개의 부서에 물품을 지원할 수 있는지 

<알고리즘>
** Greedy 사용 **
    - '최대' 많은 부서를 지원하려면, 신청 금액이 가장 '작은' 부서부터 순서대로 예산을 나누어 주어야 합니다.
    - 작은 값부터 처리하기 위해 배열 d를 오름차순으로 정렬한 뒤 순차적으로 예산에서 차감합니다.
    
[알고리즘 순서]
    1. 부서별 신청 금액 배열 d를 오름차순 정렬합니다.
    2. 정렬된 배열을 순회하면서 부서의 신청 금액을 budget에서 차감합니다.
    3. 만약 budget이 신청 금액보다 작아지면 더 이상 지원할 수 없으므로 반복을 중단합니다.
    4. 지원 가능한 부서의 총 개수를 반환합니다.
"""
def solution(d, budget):
    # 1. 오름차순 정렬 (가장 적은 금액을 신청한 부서부터 지원하기 위함)
    d.sort()
    
    answer = 0
    
    # 2. 정렬된 신청 금액을 하나씩 확인
    for amount in d:
        # 현재 예산으로 이 부서를 지원할 수 있는지 확인
        if budget >= amount:
            budget -= amount # 예산에서 차감
            answer += 1 # 지원한 부서 수 증가
        else:
            # 예산이 부족하면 뒤의 부서들은 지원 불가하므로 종료
            break
    return answer

