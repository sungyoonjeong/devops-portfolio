def solution(arr1, arr2):
    # 원래 주어졌던 초기 형식 구조를 바탕으로 빈 리스트로 초기화하여 시작합니다.
    answer = []
    
    """
    [문제 설명]
    - arr1, arr2 : 행과 열의 크기가 같은 2개의 2차원 배열(행렬)
    - return : 같은 행, 같은 열의 값을 서로 더한 결과 행렬
    
    [알고리즘 개념: 2차원 배열 순회 (Nested Loop)]
    - 행렬은 가로(행)와 세로(열)로 이루어진 2차원 구조입니다.
    - 행의 개수만큼 바깥쪽 반복문이 돌고, 각 행 안에서 열의 개수만큼 안쪽 반복문이 도는 이중 for문을 사용합니다.
    - 파이썬에서 행의 개수는 `len(arr1)`로, 열의 개수는 `len(arr1[0])`으로 구할 수 있습니다.

    [알고리즘 순서]
    1. 행렬의 세로 크기(행의 개수)만큼 반복하는 바깥쪽 for문을 만듭니다.
    2. 각 행마다 새로운 결과를 담을 빈 리스트(`row_list`)를 생성합니다.
    3. 행렬의 가로 크기(열의 개수)만큼 반복하는 안쪽 for문을 만듭니다.
    4. `arr1[i][j]`와 `arr2[i][j]`의 값을 더해 `row_list`에 추가(append)합니다.
    5. 한 행의 계산이 끝나면 완성된 `row_list`를 최종 `answer`에 추가합니다.
    """
    
    # 1. 행(세로)의 크기만큼 반복
    for i in range(len(arr1)):
        row_list = []  # 2. 이번 행의 더한 결과들을 담을 임시 리스트
        
        # 3. 열(가로)의 크기만큼 반복
        for j in range(len(arr1[0])):
            # 4. 같은 위치의 원소끼리 더해서 행 리스트에 추가
            row_list.append(arr1[i][j] + arr2[i][j])
            
        # 5. 완성된 하나의 행을 결과 행렬에 추가
        answer.append(row_list)
        
    return answer


