"""
마트에 빈 병 a개를 가져다주면 새 콜라 b병을 줍니다.
상빈이가 처음에 가지고 있는 빈 병의 개수는 n개입니다.
보유 중인 빈 병이 a개 미만이 될 때까지 이 교환을 무한 반복할 때, 
상빈이가 추가로 받아 마신 콜라의 총합을 구해야 합니다.
"""
"""
Step 1) 상빈이가 마트에서 새로 받은 콜라의 총 개수를 누적할 변수 answer를 0으로 초기화합니다.

Step 2) 현재 가지고 있는 빈 병의 개수 n이 마트에 줘야 하는 최소 개수 a보다 크거나 같은 동안 계속 반복(while)합니다.

Step 3) 반복문 내부에서 이번 회차에 새로 받을 수 있는 콜라의 양을 계산합니다:

    - 마트에 줄 수 있는 묶음의 수: n // a
    - 새로 받는 콜라 병 수: (n // a) * b

Step 4) 새로 받은 콜라 수를 answer에 더해줍니다.

Step 5) 다음 교환을 위해 상빈이가 가지게 될 새로운 빈 병의 총개수(n)를 업데이트합니다:
    - 마트에 주지 못하고 남은 빈 병: n % a
    - 새로 받아 마셔서 생긴 빈 병: (n // a) * b
    - 즉, n = (n % a) + ((n // a) * b) 가 됩니다.

Step 6) 빈 병이 a개 미만이 되어 루프가 종료되면 누적된 answer를 반환합니다.
"""
def solution(a, b, n):
    answer = 0  # 상빈이가 마트에서 새로 받은 콜라의 총 병 수를 저장할 변수입니다.
    
    # 가지고 있는 빈 병의 개수(n)가 마트에 가져다줘야 하는 기준(a) 이상인 동안 계속 반복합니다.
    while n >= a:
        # 이번 단계에서 새로 교환받는 콜라의 개수를 계산합니다.
        # (n // a)는 마트에 보낼 수 있는 묶음 수이며, 여기에 한 번에 주는 콜라 수(b)를 곱합니다.
        new_coke = (n // a) * b
        
        # 교환받은 콜라의 개수를 총 합계(answer)에 누적합니다.
        answer += new_coke
        
        # 다음 단계에서 쓸 수 있는 총 빈 병의 개수(n)를 갱신합니다.
        # 이번에 마트에 주지 못하고 남은 병(n % a)과 새로 받아서 마셔버린 빈 병(new_coke)을 더합니다.
        n = (n % a) + new_coke
        
    # 더 이상 콜라를 바꿀 수 없을 때(n < a), 지금까지 모은 콜라의 총 개수를 반환합니다.
    return answer


