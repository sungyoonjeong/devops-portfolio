# 해설진들은 선수들이 자기 바로 앞의 선수를 추월할때, 추월"한" 선수의 이름 호명
# 호명하는 선수가 앞선수와 등수가 변경됨

# player : 1등부터 현재 등수 순서대로 선수들의 이름이 담긴 문자열
# callings : 해설진이 부른 이름을 담은 문자열
# 경주가 끝났을때 선수들의 이름을 1등부터 등수 순서대로 배열에 담아 return하는 solution

def solution(players, callings):
    # ------------------------------------------------------------------------
    # [개념 1] "선수 이름"을 대면 "현재 등수(방 번호)"를 알려주는 실시간 사전 만들기
    # ------------------------------------------------------------------------
    # 컴퓨터는 players 리스트에서 "mumu"라는 선수가 몇 등인지 찾으려면 
    # 맨 앞 방부터 맨 뒤 방까지 하나씩 다 열어봐야 해서 시간이 엄청 오래 걸립니다.
    # 그래서 "이름을 주면 -> 몇 등(인덱스)인지" 한 번에 답해주는 사전을 만듭니다.
    # 
    # - enumerate(players)는 순서대로 (0등, "mumu"), (1등, "soe") 이렇게 묶어주는 도구입니다.
    # - {player: i for ...}를 거치면 {"mumu": 0, "soe": 1, "poe": 2} 같은 사전이 완성됩니다.
    p_idx = {player: i for i, player in enumerate(players)}
    
    
    # ------------------------------------------------------------------------
    # [개념 2] 해설자가 이름을 부를 때마다 실시간으로 순위 바꾸기
    # ------------------------------------------------------------------------
    # callings 리스트에 담긴 호명된 이름들을 하나씩 꺼내서 반복합니다.
    for name in callings:
        
        # 1. 방금 불린 선수의 '현재 등수'를 위에서 만든 사전에서 쏙 꺼내옵니다.
        # 예: "soe"를 불렀다면, 사전을 보고 curr = 1 임을 한 번에 알아냅니다.
        curr = p_idx[name]
        
        # 2. 이 선수가 추월할 '바로 앞 선수'의 등수는 현재 등수에서 1을 빼면 됩니다.
        # 예: 1등인 "soe"의 앞 선수는 0등(prev = 0)입니다.
        prev = curr - 1
        
        # 3. [진짜 경기장(리스트)에서 두 선수의 자리를 바꿉니다]
        # 파이썬에서는 'A, B = B, A' 라고 쓰면 양손으로 물건을 바꾸듯 자리가 동시에 바뀝니다.
        # players[0] 자리에 있던 선수와 players[1] 자리에 있던 선수의 위치가 교환됩니다.
        # 결과적으로 리스트 안의 순서가 ["mumu", "soe"]에서 ["soe", "mumu"]로 바뀝니다.
        players[prev], players[curr] = players[curr], players[prev]
        
        # 4. [경기장 자리가 바뀌었으니, 전광판 사전(p_idx)도 최신 등수로 고쳐줍니다]
        # 자리가 바뀌었으므로 이제 players[curr] 자리에 있는 선수는 '현재 등수(curr)'가 되고,
        # players[prev] 자리에 있는 선수는 '앞 등수(prev)'가 된다고 사전에 명시해 줍니다.
        # 예: 사전에 "mumu": 1, "soe": 0 으로 등수 숫자를 업데이트하는 작업입니다.
        p_idx[players[curr]], p_idx[players[prev]] = curr, prev
        
        
    # 모든 호명(callings)이 끝나고 자리가 완벽하게 배치된 최종 players 리스트를 반환합니다.
    return players


# ================= [ 코드가 진짜 잘 굴러가는지 실행해보기 ] =================
if __name__ == "__main__":
    # 1. 처음 출발 선상의 선수들 (0등: mumu, 1등: soe, 2등: poe)
    initial_players = ["mumu", "soe", "poe"]
    
    # 2. 해설자가 부른 이름 (soe 선수가 mumu 선수를 추월함!)
    call_list = ["soe"]
    
    # 3. 예상되는 경기 결과 (soe 선수가 0등이 되고, mumu 선수가 1등으로 밀림)
    expected_result = ["soe", "mumu", "poe"]
    
    print("=== 달리기 경주 시뮬레이션 테스트 실행 ===")
    
    # 함수 실행
    actual_result = solution(initial_players, call_list)
    
    print(f"내가 작성한 코드의 최종 등수: {actual_result}")
    print(f"문제에서 요구한 실제 정답: {expected_result}")
    print(f"최종 채점 결과: {'성공! 🎉' if actual_result == expected_result else '실패... ❌'}")
