"""
선수들은 1대1 대결
매 대결마다 음식의 종류, 양이 바뀜
준비된 음식 일렬배치
한 선수는 제일 안쪽에있는 음식부터 오른쪽으로,
다른선수는 제일 오른쪽에 있는 음식부터 왼쪽으로 진행
중앙에는 물을 배치
물을 먼저 먹는 선수가 승리

음식의 종류와 양 동일
먹는 순서도 같아야함.
칼로리 낮음 음식을 먼저 먹을 수 있게 배치
몇개의 음식은 대회에서 사용 금지

ex) 3가지 음식 준비됨. 칼로리 적은 순서대로 1번을 3개, 2번을 4개, 3번을 6개 준비
물을 편의상 0번 음식이라 정함
두 선수는 1번음식 1개, 2번음식 2개, 3번음식 3개씩 먹게되므로
음식은 "1223330333221"로 배치됨
따라서 1번음식 1개는 사용 못함

food : 음식의 양을 칼로리가 적은 순서대로 나타내는 정수 배열
return : 대회를 위한 음식의 배치를 나타내는 문자열
"""

"""
Step 1) 왼쪽 선수가 먹을 음식을 순서대로 이어 붙일 빈 문자열(left)을 생성.
Step 2) 1번 음식부터 마지막 음식까지 순회하면서, 각 음식의 개수를 2로 나눈 몫(// 2)을 구합니다. (홀수 개수일 때 1개가 자동으로 버려짐)
Step 3) 현재 음식 번호(인덱스 문자열)에 구한 몫을 곱해 left 문자열 뒤에 붙여 나갑니다.
Step 4) 최종 정답 문자열을 left + 물("0") + left를 뒤집은 문자열(left[::-1]) 순서로 합쳐서 반환합니다.
"""
def solution(food):
    answer = ""
    
    left = ""  # 왼쪽 선수가 먹을 음식의 순서를 저장할 문자열을 초기화합니다.
    
    # food[0]은 물(0번 음식)이므로 제외하고, 1번 음식부터 순서대로 순회합니다.
    for i in range(1, len(food)):
        # 현재 음식 번호인 i를 문자열로 바꾸고, 한 선수가 먹을 양(food[i] // 2)만큼 곱해서 더해줍니다.
        # 예: i가 1이고 몫이 3이면 "1" * 3 = "111"이 되어 left에 추가됩니다.
        left += str(i) * (food[i] // 2)
        
    # 최종 정답은 [왼쪽 음식 배치] + [물 "0"] + [왼쪽 음식 배치를 반대 방향으로 뒤집은 것]입니다.
    # 파이썬에서 문자열[::-1]은 문자열을 역순으로 뒤집어주는 가장 빠른 방법입니다.
    answer = left + "0" + left[::-1]
    
    return answer



