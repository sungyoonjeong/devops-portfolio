"""
[문제 설명]
    - 정수 x와 자연수 n을 입력받아, x부터 시작해 x씩 증가하는 숫자를 n개 지니는 리스트를 반환합니다.
    
[알고리즘 개념: 반복문과 등차수열]
    - x씩 증가하는 숫자는 수학적으로 등차수열을 이룹니다. i번째 순서에 더해지거나 곱해지는 규칙을 활용합니다.
    - 1부터 n까지 순서대로 반복하면서 `x * i`를 계산하면 x, x*2, x*3 ... x*n 개의 숫자를 쉽게 구할 수 있습니다.

[알고리즘 순서]
    1. 1부터 n까지 총 n번 반복하는 반복문(for)을 만듭니다. `range(1, n + 1)`을 사용합니다.
    2. 반복문이 돌 때마다 현재 순서(i)에 x를 곱한 값(`x * i`)을 구합니다.
    3. 구한 값을 `answer.append()`를 통해 리스트에 하나씩 추가합니다.
    4. n개의 숫자가 모두 담긴 `answer` 리스트를 반환합니다.
    """
def solution(x, n):
    answer = []
    
    # 1. 1부터 n까지 반복 (range(1, n+1)은 1부터 n까지의 숫자를 생성합니다)
    for i in range(1, n + 1):
        # 2 & 3. x에 i를 곱한 값을 리스트에 추가 (x가 2일 때: 2*1, 2*2, 2*3...)
        answer.append(x * i)
        
    # 4. 결과 리스트 반환
    return answer


# ==========================================
# [테스트 코드]
# ==========================================
if __name__ == "__main__":
    print("--- 테스트 케이스 실행 ---")
    
    # 예제 1
    x1, n1 = 2, 5
    result1 = solution(x1, n1)
    print(f"테스트 1 결과: {result1} (기댓값: [2, 4, 6, 8, 10]) -> {'정답' if result1 == [2, 4, 6, 8, 10] else '오답'}")
    
    # 예제 2
    x2, n2 = 4, 3
    result2 = solution(x2, n2)
    print(f"테스트 2 결과: {result2} (기댓값: [4, 8, 12]) -> {'정답' if result2 == [4, 8, 12] else '오답'}")
    
    # 예제 3 (음수인 경우)
    x3, n3 = -4, 2
    result3 = solution(x3, n3)
    print(f"테스트 3 결과: {result3} (기댓값: [-4, -8]) -> {'정답' if result3 == [-4, -8] else '오답'}")