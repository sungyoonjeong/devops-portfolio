"""
================================================================================
1. 문제 요약 (Problem Summary)
================================================================================
- 입력: 자연수 n (10,000,000,000 이하)
- 출력: 입력받은 수의 각 자릿수를 '뒤집은' 순서대로 가지는 정수 배열(리스트)
- 예시: 12345 -> [5, 4, 3, 2, 1]

================================================================================
2. 개념 및 알고리즘 (Concepts & Algorithm)
================================================================================
- 문자열 슬라이싱 ([::-1]): 파이썬에서 문자열이나 리스트를 효율적으로 뒤집는 표준 방법입니다.
- 데이터 타입 변환: 숫자를 문자열로 바꾸어(str) 자릿수별로 쪼갠 뒤, 뒤집고 나서 다시 정수(int)로 변환합니다.
- 함수형 프로그래밍 / 리스트 컴프리헨션: map()이나 리스트 표현식을 사용하여 요소를 변환합니다.
"""

# ------------------------------------------------------------------------------
# 방법 1: map() 함수 사용 (현재 작성하신 정답 코드 - 가장 깔끔함)
# ------------------------------------------------------------------------------
def solution_map(n):
    # n을 문자열로 바꿔 뒤집은 뒤([::-1]), 각 문자를 int로 변환하여 리스트로 만듭니다.
    return list(map(int, str(n)[::-1]))


# ------------------------------------------------------------------------------
# 방법 2: 리스트 컴프리헨션 사용 (파이썬에서 매우 자주 쓰이는 스타일)
# ------------------------------------------------------------------------------
def solution_comprehension(n):
    # 뒤집힌 문자열의 문자(i)를 하나씩 꺼내 정수(int)로 바꾼 리스트를 바로 생성합니다.
    return [int(i) for i in str(n)[::-1]]


# ------------------------------------------------------------------------------
# 방법 3: 반복문(for)을 사용한 직관적인 풀이
# ------------------------------------------------------------------------------
def solution_loop(n):
    answer = []
    # 문자열로 변환 후 뒤집은 문자열을 한 글자씩 순회합니다.
    for i in str(n)[::-1]:
        answer.append(int(i)) # 숫자로 변환하여 결과 리스트에 하나씩 추가합니다.
    return answer


# ================================================================================
# 3. 테스트 코드 (Test Code)
# ================================================================================
if __name__ == "__main__":
    # 테스트 케이스 정의 (입력값, 기댓값)
    test_cases = [
        (12345, [5, 4, 3, 2, 1]),
        (11325, [5, 2, 3, 1, 1]), # 정렬이 아닌 '뒤집기'임을 검증하는 케이스
        (7, [7])                  # 한 자리 수 케이스
    ]
    
    print("=== 테스트 시작 ===")
    for idx, (n, expected) in enumerate(test_cases, 1):
        res1 = solution_map(n)
        res2 = solution_comprehension(n)
        res3 = solution_loop(n)
        
        # 모든 방법이 기댓값과 일치하는지 확인
        success = (res1 == expected) and (res2 == expected) and (res3 == expected)
        print(f"테스트 {idx}: 입력값 = {n} | 기댓값 = {expected} -> 결과 = {'통과' if success else '실패'}")