"""
[문제 설명]
- 표준 입력으로 두 개의 정수 a(가로)와 b(세로)가 주어집니다.
- 별(*) 문자를 이용해 가로의 길이가 a, 세로의 길이가 b인 직사각형 형태를 출력합니다.

[알고리즘 개념: 문자열 반복 및 출력]
- Python에서는 문자열에 숫자를 곱하면 그 숫자만큼 문자열이 반복됩니다.
  예: '*' * 5  =>  '*****'
- 세로의 길이(b)만큼 이 작업을 반복문(for)을 통해 출력하거나, 줄바꿈 문자('\n')를 이용해 한 번에 출력할 수 있습니다.

[알고리즘 순서]
1. 가로의 길이 `a`만큼 별을 이어 붙인 한 줄의 문자열을 만듭니다. (`'*' * a`)
2. 세로의 길이 `b`만큼 반복(for문)하면서 1번에서 만든 별 줄을 화면에 출력합니다.
"""

# 원래 문제의 변수명(a, b)을 그대로 사용하며, 가로의 길이(n)가 a, 세로의 길이(m)가 b가 됩니다.

# input()으로 "5 3"
# 이때, input().strip()을 이용해 양쪽 공백 제거를 함
# .split(' ')으로 ['5','3']으로 쪼개고
# map(int, ...) 으로 각각 진짜 숫자 5와 3으로 변환한 변수 a,b
a, b = map(int, input().strip().split(' '))
for _ in range(b):
    print('*' * a)


