import collections

# ================= 1. 문제 요약 및 핵심 개념 =================
# * 문제 요약:
#   도넛 모양, 막대 모양, 8자 모양 그래프들이 무작위로 섞여 있습니다. 이들과 무관한 '생성된 정점'이 하나 추가되어,
#   해당 정점에서 각 그래프의 임의의 정점으로 향하는 단방향 간선이 연결되었습니다.
#   간선 정보(edges)가 주어질 때, [생성된 정점 번호, 도넛 그래프 수, 막대 그래프 수, 8자 그래프 수]를 구해야 합니다.
# 
# * 핵심 개념 (차수를 이용한 그래프 판별):
#   모든 정점의 '나가는 간선 수(out-degree)'와 '들어오는 간선 수(in-degree)'의 규칙을 파악하면 쉽게 풀립니다.
#   1. 생성된 정점: 들어오는 간선이 0개이고, 나가는 간선이 2개 이상입니다. (총 그래프 수 >= 2 조건 때문)
#   2. 막대 그래프: 무조건 '나가는 간선이 0개'인 정점이 그래프당 딱 1개 존재합니다.
#   3. 8자 그래프: 무조건 '나가는 간선이 2개이고 들어오는 간선도 2개 이상'인 중심 정점이 그래프당 딱 1개 존재합니다.
#   4. 도넛 그래프: 생성된 정점에서 뻗어나간 총 간선 수(총 그래프 수)에서 막대와 8자 그래프 수를 빼면 나머지가 도넛 그래프입니다.


# ================= 2. 변수 설명 =================
# * edges: 입력으로 주어지는 정점 간의 단방향 간선 정보 목록
# * in_out_counts: 각 정점 번호를 Key로 하고, [들어오는 간선 수, 나가는 간선 수]를 Value로 가지는 딕셔너리
# * created_node: 규칙을 통해 찾아낸 '생성된 정점'의 번호
# * donut_cnt, stick_cnt, eight_cnt: 각각 도넛, 막대, 8자 모양 그래프의 개수
# * total_graphs: 생성된 정점에서 나가는 간선의 수 (즉, 전체 그래프의 총합)


# ================= 3. 정답 및 테스트 코드 통합 박스 =================
def solution(edges):
    # 각 정점별 [들어오는 간선 수, 나가는 간선 수]를 기록할 딕셔너리를 초기화합니다.
    in_out_counts = collections.defaultdict(lambda: [0, 0])
    
    # 간선 정보를 하나씩 읽으며 각 정점의 진입/진출 차수를 누적합니다.
    for start, end in edges:
        # 출발 정점(start)의 나가는 간선 수(인덱스 1)를 1 증가시킵니다.
        in_out_counts[start][1] += 1
        # 도착 정점(end)의 들어오는 간선 수(인덱스 0)를 1 증가시킵니다.
        in_out_counts[end][0] += 1
        
    # 찾아낼 정답 변수들을 0으로 초기화합니다.
    created_node = 0
    donut_cnt = 0
    stick_cnt = 0
    eight_cnt = 0
    
    # 차수 기록 딕셔너리에 등록된 모든 정점을 하나씩 순회하며 특징을 검사합니다.
    for node, counts in in_out_counts.items():
        in_deg = counts[0]   # 현재 정점으로 들어오는 간선의 개수
        out_deg = counts[1]  # 현재 정점에서 나가는 간선의 개수
        
        # 조건 1: 들어오는 간선이 없고, 나가는 간선이 2개 이상이면 '생성된 정점'입니다.
        if in_deg == 0 and out_deg >= 2:
            created_node = node
        # 조건 2: 나가는 간선이 0개라면, 해당 정점은 '막대 모양 그래프'의 끝부분입니다.
        elif out_deg == 0:
            stick_cnt += 1
        # 조건 3: 나가는 간선이 2개이고 들어오는 간선이 2개 이상이면 '8자 모양 그래프'의 중심점입니다.
        elif out_deg == 2 and in_deg >= 2:
            eight_cnt += 1
            
    # 생성된 정점에서 나간 간선의 총개수가 곧 전체 그래프의 개수(도넛 + 막대 + 8자)가 됩니다.
    total_graphs = in_out_counts[created_node][1]
    
    # 전체 그래프 개수에서 판별이 명확한 막대와 8자 개수를 빼면 나머지가 도넛 그래프의 개수가 됩니다.
    donut_cnt = total_graphs - (stick_cnt + eight_cnt)
    
    # 문제에서 요구한 형식대로 배열에 담아 반환합니다.
    return [created_node, donut_cnt, stick_cnt, eight_cnt]


# ================= 테스트 코드 =================
if __name__ == "__main__":
    print("--- 테스트 케이스 실행 ---")
    
