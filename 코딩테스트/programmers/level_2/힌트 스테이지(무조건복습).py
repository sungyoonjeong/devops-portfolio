"""
================================================================================
[ 1. 문제 요약 (Problem Summary) ]
- 1번부터 n번까지의 스테이지를 순서대로 클리어해야 하는 게임이 있습니다.
- 각 스테이지를 클리어할 때 '힌트권'을 많이 보유하고 있을수록 클리어 비용이 파격적으로 줄어듭니다.
- 마지막 스테이지를 제외한 각 스테이지에서는, 다음 스테이지들에서 쓸 수 있는 힌트 카드가 들어있는 
  '힌트 번들'을 일정 금액(hint[i][0])을 내고 살지 말지 선택할 수 있습니다.
- 한 번 지나간 스테이지의 번들은 다시 살 수 없으며, 번들을 살지 말지에 대한 선택지는 단 2개입니다.
- 목표: 모든 스테이지를 클리어하는 데 필요한 최소 총비용(클리어 비용 + 번들 구매 비용)을 구해야 합니다.

================================================================================
[ 2. 입력값(매개변수) 의미 상세 요약 ]
- cost (2차원 배열) : cost[i][j]는 i번 스테이지를 깰 때, j개의 힌트권을 가지고 있을 때의 클리어 비용입니다.
                       (행: 스테이지 번호 0 ~ n-1 / 열: 가지고 있는 힌트 개수 0 ~ n-1)
- hint (2차원 배열) : 각 스테이지에서 판매하는 힌트 번들의 정보입니다.
                       hint[i][0]은 i번 스테이지에서 파는 번들의 '가격'이며,
                       hint[i][1]부터 끝까지는 그 번들을 샀을 때 얻게 되는 '힌트 카드들이 적용될 스테이지 번호'입니다.

================================================================================
[ 3. 알고리즘 실행 순서 (Step-by-Step) ]
- Step 1 [메모장 및 DP 초기화] : 중복 계산을 막기 위해 현재 스테이지와 번들 구매 상태를 기록할 memo 딕셔너리를 만듭니다.
- Step 2 [DFS 탐색 시작] : 0번 스테이지에서 아무 번들도 사지 않은 최초 상태(mask = 0)로 탐색을 출발합니다.
- Step 3 [현재 힌트 개수 계산] : 비트마스크(mask)를 확인하여 과거에 샀던 번들 중 현재 스테이지에 해당하는 힌트가 몇 개인지 누적합니다.
- Step 4 [기저 조건 및 예외 처리] : 마지막 스테이지라면 번들을 살 수 없으므로 클리어 비용만 더하고 탐색을 끝냅니다.
- Step 5 [두 갈래 길 DFS 확장] : 마지막이 아니라면 번들을 '안 사는 경우'와 '사고 비트를 켜는 경우'를 모두 호출합니다.
- Step 6 [메모이제이션 및 최솟값 반환] : 이미 계산해 본 조합 상태라면 재연산 없이 메모장에서 꺼내 쓰고, 최종 최솟값을 반환합니다.

================================================================================
[ 4. 초보자를 위한 핵심 알고리즘 개념 공부방 ]

💡 개념 1: DFS (Deep First Search - 깊이 우선 탐색)
   - 의미: 갈 수 있는 한 방향으로 끝까지 파고들며 탐색하는 방법입니다.
   - 이 문제에서의 활용: "이번 스테이지 번들을 살까? 말까?" 갈림길이 나올 때마다 일단 n번 스테이지 끝까지 
     구매/미구매 선택을 유지하며 파고들어 하나의 완성된 비용 시나리오를 만들어내는 데 사용됩니다.

💡 개념 2: 메모이제이션 (Memoization)
   - 의미: 한 번 계산한 정답을 메모장(배열이나 딕셔너리)에 적어두고, 똑같은 계산이 필요할 때 재사용하는 기술입니다.
   - 이 문제에서의 활용: "3번 스테이지인데 1번 번들은 사고 2번 번들은 안 산 상태"가 탐색 중에 또 찾아오면, 
     다시 뒤쪽 스테이지들을 계산하지 않고 메모장에서 "이 상태의 최소 비용은 얼마였지!" 하고 0.0001초 만에 꺼내 씁니다.

💡 개념 3: 비트마스크 (Bitmask)
   - 의미: 이진수의 각 자리(0 또는 1)를 스위치처럼 사용하여 여러 개의 상태(True/False)를 숫자 단 하나로 압축하는 기법입니다.
   - 이 문제에서의 활용: 어떤 번들을 샀는지 리스트로 기록하면 메모장(Key)으로 쓰기 무겁고 까다롭습니다. 
     대신 이진수 `0101`처럼 "0번 삼, 1번 안 삼, 2번 삼"을 가리키는 하나의 정수 숫자로 변환해 가볍고 빠르게 상태를 관리합니다.
================================================================================
"""

def solution(cost, hint):
    n = len(cost)  # 전체 스테이지의 개수 (2 <= n <= 16)
    
    # [Step 1] 중복 계산을 방지하기 위한 메모이제이션(DP) 전용 메모장(딕셔너리)을 선언합니다.
    # key: (현재 스테이지 번호, 지금까지의 번들 구매 상태를 나타내는 비트마스크 숫자)
    # value: 그 상태에서 최종 스테이지까지 깨는데 필요한 최소 비용
    memo = {}
    
    # [Step 2 ~ 5 구현] DFS 재귀 함수입니다.
    # stage_idx: 현재 클리어해야 하는 스테이지 번호 (0부터 시작)
    # mask: 지금까지 어떤 번들을 구매했는지 0과 1의 비트로 기록한 정수 숫자
    def get_min_cost(stage_idx, mask):
        # 모든 스테이지를 다 돌고 범위를 벗어났다면 추가 비용은 0입니다.
        if stage_idx == n:
            return 0
            
        # [Step 6 구현 - 메모이제이션 확인] 이미 완전히 똑같은 상황을 계산한 적이 있다면?
        if (stage_idx, mask) in memo:
            return memo[(stage_idx, mask)]  # 다시 계산하지 않고 메모장에서 정답을 바로 반환합니다.
            
        # [Step 3] 과거에 구매했던 번들들을 복기하며, 현재 스테이지에서 쓸 수 있는 힌트권의 총개수를 계산합니다.
        current_hints = 0
        for prev_stage in range(stage_idx):
            # 비트 연산자(&): mask의 prev_stage 번째 자리가 1(구매함)인지 확인합니다.
            if (mask & (1 << prev_stage)):
                # 내가 샀던 과거 번들의 내용물(hint[prev_stage][1:])을 열어봅니다.
                for hint_num in hint[prev_stage][1:]:
                    # 그 힌트 카드가 현재 스테이지 번호(stage_idx + 1)와 일치한다면 갯수를 추가합니다.
                    if hint_num == stage_idx + 1:
                        current_hints += 1
                        
        # 규칙: 한 스테이지에서 쓸 수 있는 힌트권은 최대 n-1개이므로 그 이상은 버립니다.
        hints_to_use = min(current_hints, n - 1)
        
        # 현재 스테이지를 클리어하는 데 필요한 순수 비용을 cost 테이블에서 가져옵니다.
        stage_clear_cost = cost[stage_idx][hints_to_use]
        
        # [Step 4] 만약 마지막 스테이지라면, 더 이상 살 수 있는 힌트 번들이 존재하지 않으므로 클리어 비용만 더하고 마칩니다.
        if stage_idx == n - 1:
            res = stage_clear_cost + get_min_cost(stage_idx + 1, mask)
            memo[(stage_idx, mask)] = res
            return res
            
        # [Step 5] 마지막 스테이지가 아니라면, 번들을 '안 사는 길'과 '사는 길' 두 갈래로 DFS 탐색을 뻗어나갑니다.
        
        # [갈래 ①] 현재 스테이지의 힌트 번들을 구매하지 않고 패스하는 경우
        # 비용은 현재 스테이지 클리어 비용만 지불하고, 구매 상태(mask)는 그대로 유지한 채 다음 스테이지로 진입합니다.
        option_not_buy = stage_clear_cost + get_min_cost(stage_idx + 1, mask)
        
        # [갈래 ②] 현재 스테이지의 힌트 번들을 돈을 내고 새로 구매하는 경우
        # 비용은 현재 스테이지 클리어 비용 + 번들 가격(hint[stage_idx][0])이 함께 발생합니다.
        bundle_price = hint[stage_idx][0]
        # 비트 연산자(|): 기존 mask의 stage_idx 번째 자리를 1(구매함)로 바꾼 새로운 비트 상태를 생성합니다.
        new_mask = mask | (1 << stage_idx)
        option_buy = stage_clear_cost + bundle_price + get_min_cost(stage_idx + 1, new_mask)
        
        # 두 갈래 길 중에서 최종 누적 비용이 더 적게 드는 최솟값을 선택해 메모장에 기록합니다.
        memo[(stage_idx, mask)] = min(option_not_buy, option_buy)
        return memo[(stage_idx, mask)]

    # 가장 처음 상태인 0번 스테이지, 번들을 아무것도 사지 않은 상태(mask = 0)부터 출발합니다!
    return get_min_cost(0, 0)


