"""
1. 물류센터에는 (r,c)와 같이 좌표로 나타낼수 있는 n개의 포인트 존재.(1~n)
2. 로봇마다 정해진 운송경로 존재. 운송경로는 m개의 포인트로 구성. 로봇은 첫포인트에서 시작해 할당된 포인트를 순서대로 방문.
3. 로봇은 x대. 모든로봇은 0초에 동시에 출발. 로봇은 1초마다 r좌표와 c좌표 중 하나가 1만큼 감소하거나 증가한 좌표로 이동 가능
4. 다음 포인트로 이동할 때는 항상 최단 경로로 이동하여 최단 경로가 여러가지일 경우, r좌표가 변하는 이동을 c좌표가 변하는 이동보다 먼저
5. 마지막 포인트에 도착한 로봇은 운송을 마치고 물류센터를 벗어남. 벗어나는 경로는 고려하지 않음

같은 좌표에 로봇이 2대 이상 모인다면 충돌할 가능성이 있는 위험상황으로 판단.
관리자(나)는 현재 설정대로 로봇이 움직일대 위험한 상황이 총 몇 번 일어나는지 알고싶음.
만약 어떤 시간에 여러 좌표에서 위험 상황이 발생한다면 그 횟수를 모두 더함.

points : 운송 포인트 n개의 좌표를 담은 2차원 정수 배열
x : 로봇 대수
routes : 운송경로
return : 모든 로봇이 운송을 마칠 때까지 발생하는 위험한 상황의 횟수

<알고리즘 순서>
1. 로봇별 전체 이동 경로(좌표 히스토리) 생성:
    - 각 로봇이 출발 포인트에서 시작하여 다음 포인트들로 이동할 때의 초단위 위치 좌표를 순서대로 계산하여 리스트에 저장합니다.
    - 이동할 때 문제 조건에 따라 r 좌표(행)를 먼저 이동시키고, 그 다음 c 좌표(열)를 이동시킵니다.
2. 시간대별 위치 기록:
    - 모든 로봇의 이동 경로를 구했다면, 시간(t = 0, 1, 2, ...)별로 각 좌표에 로봇이 몇 대 있는지 체크해야 합니다.
3. 충돌 위험 계산:
    - 각 시간대마다 좌표별 로봇의 수를 세어, 동일한 시간과 동일한 좌표에 2대 이상의 로봇이 존재하는 경우 위험 상황(danger_count += 1)으로 판단합니다.
4. 결과 반환: 모든 로봇이 운송을 마치는 시간까지 발생한 총 위험 상황 횟수를 반환합니다.

<핵심 개념 설명>
1) 시뮬레이션 및 해시 맵(Counter):
    - 이 문제는 초 단위로 로봇의 움직임을 완벽히 구현하는 시뮬레이션 문제입니다. 최단 경로 이동 시 r이 c보다 먼저 변한다는 규칙을 구현하는 것이 핵심입니다. 각 시간대별로 로봇들의 위치를 파악할 때 collections.Counter를 사용하면 어떤 좌표에 로봇이 2대 이상 모였는지 O(N)으로 쉽게 집계할 수 있습니다.
"""
import collections
def solution(points, routes):
    # 각 로봇의 전체 이동 경로(초 단위 좌표 리스트)들을 담을 배열입니다.
    all_robot_paths = []
    
    # 각 로봇의 운송 경로(route)를 하나씩 꺼내어 전체 이동 동선을 구합니다.
    for route in routes:
        # 해당 로봇의 실시간 좌표 기록을 저장할 리스트입니다.
        path = []
        
        # 로봇이 방문해야 하는 포인트들을 순서대로 확인합니다.
        for i in range(len(route) - 1):
            # 현재 출발 포인트의 번호를 가져옵니다 (1-indexed이므로 1을 뺍니다).
            start_point_idx = route[i] - 1
            # 다음 목적지 포인트의 번호를 가져옵니다.
            end_point_idx = route[i + 1] - 1
            
            # 출발 포인트의 [r, c] 실제 좌표를 가져옵니다.
            curr_r, curr_c = points[start_point_idx]
            # 목적지 포인트의 [r, c] 실제 좌표를 가져옵니다.
            next_r, next_c = points[end_point_idx]
            
            # 경로의 맨 처음 시작점일 때만 현재 위치를 경로에 추가합니다. (이후는 이동하면서 추가)
            if i == 0:
                path.append((curr_r, curr_c))
            
            # 1. r 좌표를 먼저 목적지 좌표와 같아질 때까지 이동시킵니다.
            while curr_r != next_r:
                # 목적지가 더 크면 r을 1 증가시키고, 작으면 1 감소시킵니다.
                curr_r += 1 if next_r > curr_r else -1
                # 이동한 위치의 좌표를 로봇의 경로에 추가합니다.
                path.append((curr_r, curr_c))
                
            # 2. r 좌표가 맞춰지면 c 좌표를 목적지 좌표와 같아질 때까지 이동시킵니다.
            while curr_c != next_c:
                # 목적지가 더 크면 c을 1 증가시키고, 작으면 1 감소시킵니다.
                curr_c += 1 if next_c > curr_c else -1
                # 이동한 위치의 좌표를 로봇의 경로에 추가합니다.
                path.append((curr_r, curr_c))
                
        # 한 로봇의 전체 초 단위 이동 경로가 완성되면 전체 리스트에 추가합니다.
        all_robot_paths.append(path)
        
    # 모든 로봇 중 가장 긴 이동 시간을 구하여 시뮬레이션의 최대 시간 범위를 정합니다.
    max_time = max(len(p) for p in all_robot_paths)
    # 총 충돌 위험 상황 횟수를 저장할 변수입니다.
    danger_count = 0
    
    # 0초부터 가장 마지막에 멈추는 로봇의 시간까지 1초씩 흘려보내며 확인합니다.
    for t in range(max_time):
        # 현재 시간(t)에 존재하는 로봇들의 위치 좌표를 모아둘 리스트입니다.
        current_positions = []
        
        # 모든 로봇을 순회하며 현재 시간(t)의 위치를 확인합니다.
        for path in all_robot_paths:
            # 로봇이 아직 이동 중인 상태(경로 길이보다 t가 작음)라면 현재 위치를 수집합니다.
            if t < len(path):
                current_positions.append(path[t])
                
        # 현재 시간대에 각 좌표별로 로봇이 몇 대 있는지 개수를 세어 딕셔너리 형태로 만듭니다.
        position_counts = collections.Counter(current_positions)
        
        # 각 좌표에 모인 로봇의 대수를 확인합니다.
        for count in position_counts.values():
            # 같은 좌표에 로봇이 2대 이상 모여 있다면 위험 상황입니다.
            if count >= 2:
                # 위험 상황 발생 횟수를 1 증가시킵니다.
                danger_count += 1
                
    # 누적된 총 위험 상황 횟수를 반환합니다.
    return danger_count


# ================= 테스트 코드 =================
