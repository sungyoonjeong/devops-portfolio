"""
================================================================================
[ 1. 문제 요약 (Problem Summary) ]
- m x n 크기의 사막 격자가 있고, 이곳에 가로 w, 세로 h 크기의 선인장 구역을 만들려고 합니다.
- 비구름이 먹구름을 몰고 와 정해진 순서(drops)대로 격자에 빗방울을 한 방울씩 떨어뜨립니다.
- 선인장 구역 내의 칸 중 '단 한 칸'이라도 비를 맞으면 선인장이 비를 맞기 시작한 것으로 간주합니다.
- 목표: 선인장이 최대한 늦게 비를 맞도록(또는 끝까지 안 맞도록) 선인장 구역의 최적의 위치를 찾아야 합니다.
- 조건: 후보가 여러 개라면 '가장 위쪽(행 인덱스가 작은 곳)', 그래도 겹치면 '가장 왼쪽(열 인덱스가 작은 곳)'을 선택합니다.

================================================================================
[ 2. 입력값(매개변수) 의미 상세 요약 ]
- m (정수) : 사막 격자의 세로 길이(행의 개수)입니다.
- n (정수) : 사막 격자의 가로 길이(열의 개수)입니다.
- h (정수) : 우리가 배치할 선인장 구역의 세로 길이입니다.
- w (정수) : 우리가 배치할 선인장 구역의 가로 길이입니다.
- drops (2차원 배열) : 비가 떨어지는 순서대로 좌표를 담은 배열입니다. `[[r1, c1], [r2, c2], ...]` 형태입니다.

================================================================================
[ 3. 알고리즘 실행 순서 (Step-by-Step) ]
- Step 1 [격자판 타임스탬프 초기화] : 사막판을 끝까지 비가 안 내리는 가상의 시간(len(drops) + 1)으로 채워 생성합니다.
- Step 2 [비 내리기 구현] : drops 배열을 순회하며 각 칸에 비가 내린 순서(1, 2, 3...)를 기록합니다.
- Step 3 [이분 탐색 기준 설정] : 0번째 비(비 안 옴)부터 마지막 비(다 옴)까지 범위를 잡고 중간 시점(mid)을 구합니다.
- Step 4 [안전 지도 필터링] : check(mid) 함수 안에서 mid 시점 이하에 비를 맞은 칸은 1, 안 맞은 칸은 0인 지도를 그립니다.
- Step 5 [2차원 누적 합 구축] : 빠르게 사각형 구역을 검사하기 위해 0과 1로 구성된 지도를 누적합(Prefix Sum) 배열로 만듭니다.
- Step 6 [선인장 도장 깨기] : h x w 크기 도장으로 사막을 훑으며 비를 한 방울도 안 맞은 명당(누적합 = 0)이 있는지 찾습니다.
- Step 7 [범위 조절 및 최종 좌표 반환] : 명당이 있다면 시간을 늘려보고, 없다면 줄여가며 최종 최대 안전 시간을 구한 뒤 정답 좌표를 뽑아냅니다.

================================================================================
[ 4. 초보자를 위한 핵심 알고리즘 개념 공부방 ]

💡 개념 1: 이분 탐색 (Binary Search / Parametric Search)
   - 의미: 정렬된 구간에서 찾고자 하는 값의 범위를 매번 절반씩 뚝뚝 잘라가며 정답을 찾아내는 업앤다운(Up & Down) 기법입니다.
   - 이 문제에서의 활용: "1번째 비 내릴 때 버티나? 2번째 때는?" 하며 하나씩 보면 격자가 너무 커서 시간 초과가 납니다.
     따라서 "중간인 mid번째 비가 내릴 때 버티나?"를 시험한 뒤, 버티면 범위를 뒤로 늘리고 못 버티면 앞으로 당깁니다.

💡 개념 2: 2차원 누적 합 (2D Prefix Sum)
   - 의미: 격자에서 특정 사각형 구역의 합을 매번 일일이 더하지 않고, 미리 구해둔 누적 숫자의 뺄셈 연산만으로 O(1)만에 구하는 기술입니다.
   - 이 문제에서의 활용: 선인장 구역 h x w 안에 비 맞은 칸(1)이 섞여 있는지 사막 전체를 돌며 매번 조사하면 너무 느립니다.
     누적 합을 미리 만들어두면, 사각형의 네 꼭짓점 숫자를 더하고 빼는 것만으로 그 구역의 위험도를 단번에 알아낼 수 있습니다.
================================================================================
"""

def solution(m, n, h, w, drops):
    # [Step 1] 격자판을 무한대 시간(총 빗방울 수 + 1)으로 채워 초기화합니다.
    # 끝까지 비를 안 맞은 칸과 마지막 비(len(drops))를 맞은 칸을 컴퓨터가 식별할 수 있도록 확실하게 큰 값을 줍니다.
    rain_time = [[len(drops) + 1] * n for _ in range(m)]
    
    # [Step 2] 빗방울이 떨어지는 좌표에 '몇 번째 빗방울(i + 1)'인지 타임스탬프 도장을 찍어줍니다.
    for i, (r, c) in enumerate(drops):
        rain_time[r][c] = i + 1

    # [Step 4, 5, 6 구현] mid 시점까지 선인장이 비 안 맞고 숨을 자리가 있는지 심사하는 함수입니다.
    def check(mid):
        # 외곽 경계선 에러(IndexError)를 막고 계산을 편하게 하기 위해 상단과 좌측에 0으로 채워진 가짜 테두리를 두릅니다.
        prefix = [[0] * (n + 1) for _ in range(m + 1)]
        
        # [Step 4] 현재 시험 중인 기준 시간(mid) 이전에 이미 비를 맞은 위험 칸은 1, 안전 칸은 0으로 필터링합니다.
        for r in range(m):
            for c in range(n):
                if rain_time[r][c] <= mid:
                    prefix[r + 1][c + 1] = 1 # 가짜 테두리 한 줄 때문에 인덱스를 하나씩 밀어서 임시 기록합니다.
        
        # [Step 5] 2차원 누적 합 배열을 만듭니다. (내 위쪽 칸 + 내 왼쪽 칸 값을 더하고 대각선 중복값을 뺍니다)
        for i in range(1, m + 1):
            for j in range(1, n + 1):
                prefix[i][j] += prefix[i - 1][j] + prefix[i][j - 1] - prefix[i - 1][j - 1]
        
        # [Step 6] h x w 크기의 선인장 도장을 찍으며 구역 내부의 빗방울 합을 단 O(1)만에 계산합니다.
        for r in range(m - h + 1):
            for c in range(n - w + 1):
                # 사각형 구간 합 공식: 오른쪽아래 - 위쪽제외 - 왼쪽제외 + 중복보정
                total_rain_in_zone = (prefix[r + h][c + w] 
                                      - prefix[r][c + w] 
                                      - prefix[r + h][c] 
                                      + prefix[r][c])
                
                # 구역 내에 빗방울 수가 0개라면 mid 시점까지 완벽히 보송보송하게 살아남은 명당을 찾은 것입니다!
                if total_rain_in_zone == 0:
                    return True # 숨을 곳이 있으므로 신나게 True 리턴!
        
        return False # 사막 전체를 다 찍어봤는데 무조건 비가 섞여있다면 버티지 못하므로 False 리턴!

    # [Step 3 & 7] 이분 탐색(Up & Down)으로 선인장이 버틸 수 있는 '최대 안전 시점'을 좁혀나갑니다.
    left, right = 0, len(drops) # 탐색 범위: 0번째 비(아직 안 옴) ~ 마지막 비(다 옴)
    max_safe_time = 0           # 가장 늦게까지 비를 안 맞고 버티는 시간을 기록할 변수입니다.

    while left <= right:
        mid = (left + right) // 2 # 컴퓨터가 시험해볼 중간 시점(mid)을 자동으로 계산합니다.
        
        if check(mid):            # 만약 mid 시점까지 선인장이 안전하게 숨을 곳이 하나라도 존재한다면?
            max_safe_time = mid   # 정답 후보 시점으로 기록해두고,
            left = mid + 1        # "더 늦은 시간에도 버틸 명당이 있을까?" 확인하기 위해 범위를 오른쪽으로 옮깁니다.
        else:                     # 만약 mid 시점엔 사막 전체가 비에 젖어 숨을 자리가 없다면?
            right = mid - 1       # "더 이른 시간으로 가야겠네" 하고 범위를 왼쪽으로 줄입니다.

    # [마무리 단계] 찾아낸 최종 안전 시간(max_safe_time)을 기준으로 누적 합 배열을 최종적으로 딱 한 번 더 만듭니다.
    final_prefix = [[0] * (n + 1) for _ in range(m + 1)]
    for r in range(m):
        for c in range(n):
            if rain_time[r][c] <= max_safe_time:
                final_prefix[r + 1][c + 1] = 1
                
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            final_prefix[i][j] += final_prefix[i - 1][j] + final_prefix[i][j - 1] - final_prefix[i - 1][j - 1]

    # 문제 조건("가장 위쪽 행, 그래도 겹치면 가장 왼쪽 열")을 만족하는 최초의 안전 명당 좌표 [r, c]를 찾습니다.
    # r(행) 루프가 바깥에 있고 c(열) 루프가 안에 있으므로, 조건에 부합하는 첫 명당이 걸리는 즉시 정답이 됩니다.
    for r in range(m - h + 1):
        for c in range(n - w + 1):
            total_rain = (final_prefix[r + h][c + w] 
                          - final_prefix[r][c + w] 
                          - final_prefix[r + h][c] 
                          + final_prefix[r][c])
            if total_rain == 0:
                return [r, c] # 최적의 명당 왼쪽 위 꼭짓점 좌표 [행, 열]을 리턴합니다.


