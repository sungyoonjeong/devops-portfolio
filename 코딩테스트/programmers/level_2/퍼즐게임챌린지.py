"""
순서대로 n개의 퍼즐을 제한 시간 내에 풀어야 하는 퍼즐게임중
각 퍼즐은 난이도와 소요시간이 정해져있음.
숙련도에 따라 퍼즐을 풀 때 틀리는 횟수 바뀜

diff : 현재 퍼즐의 난이도
time_cur : 현재 퍼즐의 소요 시간
time_prev : 이전 퍼즐의 소요 시간
level : 당신의 숙련도
limit : 전체 제한시간
times : 퍼즐의 소요시간을 순서대로 담은 정수 배열


1) diff <= level 이면 퍼즐을 틀리지 않고, time_cur만큼의 시간을 사용하여 해결
2) diff>level 이면, 퍼즐을 총 diff-level번 틀림. 퍼즐을 틀릴때마다 time_cur만큼의 시간을 사용, 추가로 time_prev만큼의 시간을 사용하여 이전 퍼즐을 다시 풀고와야함. 이전 퍼즐을 다시 풀때는 이전 퍼즐의 난이도에 상관없이 틀리지 않는다. 

EX) diff = 3, time_cur = 2, time_prev = 4인 경우
    - level = 1, 퍼즐을 3-1=2번 틀림. 한번 틀릴때마다 2+4=6의 시간을 사용. 다시 퍼즐을 푸는데 2의 시간을 사용하므로 총 6*2+2=14시간을 사용
    
limit :  전체 제한 시간.
제한 시간 내에 퍼즐을 모두 해결하기 위한 숙련도의 최솟값을 구하려고함. 
난이도, 소요시간은 모두 양의 정수
숙련도 또한 양의 정수

<알고리즘 순서>
1) 탐색 범위 설정 : 숙련도(level)의 최솟값은 1, 최대값은 난이도의 최대치인 max(diffs)
2) 이진 탐색 : left = 1, right = max(diffs)로 초기화한 뒤 중간값 mid를 현재 숙련도로 가정하고 시뮬레이션함
3) 시간 계산 : 현재 숙련도 mid로 모든 퍼즐을 풀었을때 걸리는 총 소요시간 계산
    - 난이도가 숙련도 이하(diff<=mid)면 time_cur만큼 소요
    - 난이도가 숙련도 초과(diff>mid)면 (diff-mid) * (time_cur + time_prev) + time_cur만큼 소요
4) 범위 좁히기 : 
    - 총 소요시간이 제한시간 limit이하이면, 더 작은 숙련도에서 가능한지 찾이 위해 왼쪽으로 좁힘.(right = mid - 1, 정답 후보로 mid기록)
    - 총 소요시간이 limit을 초과했다면, 숙련도가 부족한 것이므로 범위를 오른쪽으로 좁힘(left = mid + 1)
5) 결과 반환 : 이진 탐색이 끝나면 제한 시간을 만족하는 숙련도의 최솟값을 반환.

<핵심 개념>
이진 탐색 (Binary Search / 매개 변수 탐색):
퍼즐 개수 $n$이 최대 300,000이고 제한 시간이 $10^{15}$로 매우 큽니다. 숙련도를 1부터 순차적으로 증가시키며 확인하면 시간 초과가 발생합니다. 숙련도가 높을수록 총 소요 시간은 줄어드는 단조성을 가지므로, 이진 탐색을 통해 $O(n \log(\max(\text{diffs})))$의 효율적인 시간 복잡도로 최적의 값을 찾을 수 있습니다.
"""

def solution(diffs, times, limit):
    # 이진 탐색을 위한 시작점(최소 숙련도)을 1로 설정합니다.
    left = 1
    # 이진 탐색을 위한 끝점(최대 숙련도)을 퍼즐 중 가장 높은 난이도로 설정합니다.
    right = max(diffs)
    # 정답을 저장할 변수를 최대 난이도로 초기화합니다.
    answer = right

    # 탐색 범위가 유효할 때까지 이진 탐색을 반복합니다.
    while left <= right:
        # 현재 탐색 범위의 중간값을 현재 검사할 숙련도(mid)로 결정합니다.
        mid = left + (right - left) // 2
        
        # 현재 숙련도(mid)로 게임을 진행했을 때 걸리는 총 소요 시간을 저장할 변수입니다.
        total_time = 0
        
        # 퍼즐 배열의 길이(총 퍼즐 개수)를 구합니다.
        n = len(diffs)
        
        # 첫 번째 퍼즐부터 마지막 퍼즐까지 순서대로 탐색하며 소요 시간을 누적합니다.
        for i in range(n):
            # 현재 퍼즐의 난이도를 가져옵니다.
            diff = diffs[i]
            # 현재 퍼즐을 해결하는 데 걸리는 기본 소요 시간을 가져옵니다.
            time_cur = times[i]
            # 이전 퍼즐의 소요 시간을 가져옵니다. 첫 번째 퍼즐(i=0)인 경우 이전 퍼즐이 없으므로 0으로 처리합니다.
            time_prev = times[i - 1] if i > 0 else 0
            
            # 현재 퍼즐의 난이도가 현재 숙련도(mid) 이하인 경우입니다.
            if diff <= mid:
                # 틀리지 않고 한 번에 통과하므로 현재 퍼즐의 소요 시간만큼만 더해줍니다.
                total_time += time_cur
            # 현재 퍼즐의 난이도가 현재 숙련도(mid)보다 큰 경우입니다.
            else:
                # 퍼즐을 틀리는 횟수는 (난이도 - 숙련도)가 됩니다.
                wrong_count = diff - mid
                # 한 번 틀릴 때마다 (이전 퍼즐 시간 + 현재 퍼즐 시간)을 쓰고, 마지막에 최종 성공할 때 현재 퍼즐 시간을 씁니다.
                total_time += wrong_count * (time_cur + time_prev) + time_cur
                
            # 계산 도중 이미 제한 시간(limit)을 초과했다면 더 이상 계산할 필요가 없으므로 반복을 중단합니다.
            if total_time > limit:
                break
                
        # 모든 퍼즐을 풀고 나온 총 소요 시간이 제한 시간(limit) 이하인 경우입니다.
        if total_time <= limit:
            # 현재 숙련도로 성공했으므로 정답 후보로 저장합니다.
            answer = mid
            # 더 작은 숙련도에서도 가능한지 확인하기 위해 오른쪽 범위를 줄여 왼쪽 구간을 탐색합니다.
            right = mid - 1
        # 총 소요 시간이 제한 시간(limit)을 초과한 경우입니다.
        else:
            # 현재 숙련도로는 부족하므로 왼쪽 범위를 늘려 오른쪽 구간(더 큰 숙련도)을 탐색합니다.
            left = mid + 1
            
    # 조건(제한 시간 내 완료)을 만족하는 숙련도의 최솟값을 반환합니다.
    return answer


