"""
세로 길이 : n
가로 길이 : m
시추관을 수직으로 단 하나만 뚫을수 있음
가장 많은 석유를 뽑을 수 있는 시추관의 위치를 찾으려고 함
시추관은 열 하나를 관통하는 형태여야 하며, 열과 열 사이에 시추관을 뚫을 수 없음

land : 석유가 묻힌 땅과 석유 덩어리를 나타내는 2차원 정수배열
return : 시추관 하나를 설치해 뽑을 수 있는 가장 많은 석유량

len(land) = 세로길이
len(land[i]) = 가로 길이

<개념>
1) BFS(너비 우선 탐색) / DFS(깊이 우선 탐색) 
    : 연결된 석유 덩어리의 크기와 그 덩어리가 차지하는 열(Column)의 범위를 탐색하기 위해 사용
    : 큐를 이용한 BFS가 직관적이고 안전함
2) 시뮬레이션 / 누적합 배열 
    : 각 열마다 BFS를 새로 돌리면 효율성 테스트 통과 못함. 
    : 따라서, 하나의 석유 덩어리를 찾았을때, 이 덩어리가 속한 모든 열에 덩어리 크기를 미리 더해두는 방식을 이용해야 효율성 만족.

<알고리즘 순서>
1) 초기화 : 각 열(O부터 m-1까지)에 담길 총 석유량을 저장할 배열(col_oil)을 땅의 가로 길이(m)만큼 생성함. 방문 여부를 체크할 visited 배열도 만듬
2) 격자 탐색 : 땅을 전체 순회하며 방문하지 않은 석유(land[i][j] == 1)를 발견하면 BFS를 시작함.
3) BFS 수행(덩어리 크기 & 범위 측정)
    - 상하좌우로 이동하며 연결된 석유의 개수(크기)를 센다.
    - 동시에 이 덩어리가 어떤 열들을 거쳐가는지 확인하기 위해 집합(set)에 열 번호(j)를 기록
4) 열별 누적합 반영 : BFS가 끝나면, 기록된 모든 열 번호에 대해 col_oil[열_번호] += 덩어리_크기를 해줌
5) 최댓값 반환 : col_oil 배열에서 가장 큰 값을 찾아서 반환
"""
from collections import deque

def solution(land):
    n = len(land)     # 땅의 세로 길이
    m = len(land[0])  # 땅의 가로 길이
    
    # 각 열별로 획득할 수 있는 총 석유량을 저장할 배열
    col_oil = [0] * m
    # 방문 여부를 기록할 2차원 배열
    visited = [[False] * m for _ in range(n)]
    
    # 상하좌우 방향 벡터
    dx = [-1, 1, 0, 0]
    dy = [0, 0, -1, 1]
    
    # 격자 전체를 순회
    for i in range(n):
        for j in range(m):
            # 석유가 있고 아직 방문하지 않은 곳이라면 BFS 시작 (새로운 석유 덩어리 발견)
            if land[i][j] == 1 and not visited[i][j]:
                queue = deque([(i, j)])
                visited[i][j] = True
                
                oil_size = 0  # 현재 석유 덩어리의 크기
                columns = set()  # 현재 석유 덩어리가 걸쳐 있는 열(Column)들의 집합
                
                # BFS 탐색을 통해 덩어리 크기와 거쳐가는 열을 구함
                while queue:
                    x, y = queue.popleft()
                    oil_size += 1
                    columns.add(y)  # 덩어리가 포함된 열 번호 저장
                    
                    # 상하좌우 인접한 칸 탐색
                    for d in range(4):
                        nx, ny = x + dx[d], y + dy[d]
                        # 격자 범위를 벗어나지 않고, 석유가 있으며, 방문하지 않은 경우
                        if 0 <= nx < n and 0 <= ny < m:
                            if land[nx][ny] == 1 and not visited[nx][ny]:
                                visited[nx][ny] = True
                                queue.append((nx, ny))
                
                # BFS 종료 후, 이 덩어리가 지나는 모든 열에 덩어리 크기를 누적
                for col in columns:
                    col_oil[col] += oil_size
                    
    # 각 열의 석유량 중 가장 큰 값을 반환
    return max(col_oil)


