"""
================================================================================
[ 1. 문제 요약 (Problem Summary) ]
- 루트 노드, 리프 노드, 분배 노드로 이루어진 트리 구조를 설계해야 합니다.
- 분배 노드는 자식 노드를 무조건 '2개' 또는 '3개' 가집니다.
- 같은 깊이(레벨)에 있는 분배 노드들은 모두 자식 수가 똑같아야 합니다.
- 리프 노드의 '분배도'는 조상 분배 노드들의 자식 개수를 모두 곱한 값입니다.
- 제한사항: 총 분배 노드 개수 <= dist_limit, 모든 리프 노드의 분배도 <= split_limit
- 목표: 이 제한을 모두 지키는 가장 많은 리프 노드의 개수를 구하는 문제입니다.

================================================================================
[ 2. 입력값(매개변수) 의미 상세 요약 ]
- dist_limit (정수) : 트리 전체에 배치할 수 있는 '분배 노드의 최대 한도'입니다. (0 ~ 10억)
- split_limit (정수) : 리프 노드가 도달할 수 있는 '최대 분배도(누적 곱) 한도'입니다. (1 ~ 10억)

================================================================================
[ 3. 알고리즘 실행 순서 (Step-by-Step) ]
- Step 1 [완전 탐색 루프 설정] : 2배 분배 층의 수 x와 3배 분배 층의 수 y의 모든 조합을 조사합니다.
- Step 2 [분배도 조건 심사] : 최종 최하단 분배도 (2^x * 3^y)가 split_limit 이하인 조합만 필터링합니다.
- Step 3 [그리디(Greedy) 노드 배치 시뮬레이션] : 
     분배 노드 소모를 최소화하기 위해, 무조건 상위 층에 2배 분배(x번)를 먼저 채우고, 
     그 아래 하위 층에 3배 분배(y번)를 이어서 배치합니다.
- Step 4 [정밀 쪼개기 연산] : 각 층에서 전체 노드를 다 쪼갤 필요 없이, 내가 가진 남은 분배 노드 한도(rem_dist)와 
                             현재 층의 노드 수(nodes) 중 최솟값만큼만 정밀하게 쪼개어 리프를 늘려나갑니다.
- Step 5 [최댓값 갱신 및 리턴] : 모든 조합 중 가장 많은 리프 노드를 만들어낸 값을 정답으로 반환합니다.

================================================================================
[ 4. 초보자를 위한 핵심 알고리즘 개념 공부방 ]

💡 개념 1: 수학적 최적 순서 (그리디)
   - 의미: 자식을 많이 늘리는 연산을 나중에 할수록, 상위 레벨에서 소모되는 분배 노드의 총합이 드라마틱하게 절약됩니다.
   - 이 문제에서의 활용: 2배 분배를 먼저 배치해 노드가 서서히 늘어나도록 제어한 뒤, 3배 분배를 나중에 섞어주어야 
     제한된 dist_limit 내에서 가장 효율적이고 거대한 트리를 설계할 수 있습니다.

💡 개념 2: 부분 분배 (Partial Splitting)
   - 의미: 한 층에 있는 노드가 100개 개 있어도, 내가 가진 돈(분배 한도)이 2개밖에 없다면 딱 2개만 쪼개고 
     나머지 98개는 그대로 리프 노드로 남겨두는 유연한 처리가 가능합니다.
================================================================================
"""

def solution(dist_limit, split_limit):
    max_leaves = 1 # 분배를 단 한 번도 하지 않았을 때(루트의 자식 1개)를 최솟값으로 둡니다.
    
    # [Step 1] 2배 분배 층의 수(x)와 3배 분배 층의 수(y)의 모든 유효 조합을 완전 탐색합니다.
    # 2^30과 3^20은 이미 10억을 상회하므로 아래 범위로 모든 케이스를 커버합니다.
    for x in range(32):
        for y in range(22):
            # [Step 2] 이 조합이 가질 수 있는 최하단 리프의 분배도 계산 및 제한 조건 심사
            if (2 ** x) * (3 ** y) > split_limit:
                continue
                
            # [Step 3 & 4] 그리디 법칙에 따라 필요한 분배 노드 수를 정밀 시뮬레이션합니다.
            rem_dist = dist_limit # 현재 조합에서 쓸 수 있는 남은 분배 노드 한도
            nodes = 1             # 루트 바로 밑층의 초기 노드 수 (1개)
            
            # 치트키 순서: 2배 분배(x번)를 상위 층에 먼저 배치합니다.
            for _ in range(x):
                # 현재 층의 노드를 다 쪼갤 한도(돈)가 남아있다면 전체를 다 2배 분배합니다.
                if rem_dist >= nodes:
                    rem_dist -= nodes
                    nodes += nodes * 1 # 2배 분배는 노드가 1배수만큼 늘어납니다 (N -> 2N)
                else:
                    # 한도가 부족하다면 남은 한도(rem_dist)만큼만 쪼개고 종료합니다.
                    nodes += rem_dist * 1
                    rem_dist = 0
                    break
                    
            # 이어서 3배 분배(y번)를 하위 층에 배치합니다.
            if rem_dist > 0:
                for _ in range(y):
                    if rem_dist >= nodes:
                        rem_dist -= nodes
                        nodes += nodes * 2 # 3배 분배는 노드가 2배수만큼 늘어납니다 (N -> 3N)
                    else:
                        # 한도가 부족하다면 남은 한도만큼만 쪼개고 종료합니다.
                        nodes += rem_dist * 2
                        rem_dist = 0
                        break
                        
            # [Step 5] 최종적으로 살아남은 리프 노드의 수(nodes) 중 최댓값을 갱신합니다.
            max_leaves = max(max_leaves, nodes)
            
    return max_leaves


