"""
================================================================================
[ 1. 문제 요약 (Problem Summary) ]
- 두 도둑 A와 B가 보석을 훔치는데, 각 물건마다 A가 훔칠 때 남는 흔적과 B가 훔칠 때 남는 흔적이 다릅니다.
- 모든 물건은 A 또는 B 중 한 명은 반드시 훔쳐야 합니다.
- 조건: B도둑의 총 흔적 개수가 m개 '미만'이어야 합니다. (m개 이상이 되면 구속됩니다)
- 목표: 이 조건을 만족하면서 A도둑이 남기는 총 흔적 개수의 "최솟값"을 구해야 합니다.
- 만약 B의 흔적을 m 미만으로 유지하면서 모든 물건을 훔치는 방법이 없다면 -1을 반환합니다.

================================================================================
[ 2. 입력값(매개변수) 의미 상세 요약 ]
- info (2차원 배열) : 각 물건을 훔칠 때 발생하는 흔적 정보입니다. 
                      info[i][0]은 i번째 물건을 A가 훔칠 때의 흔적, info[i][1]은 B가 훔칠 때의 흔적입니다.
- n (정수) : A도둑이 달성해야 하는 흔적의 상한선입니다. (단, 이 문제에서는 B를 m 미만으로 막으면서 A를 최소화하는 것이 핵심입니다)
- m (정수) : B도둑이 걸리지 않는 흔적의 상한선입니다. (B의 총 흔적은 반드시 m 미만이어야 합니다)

================================================================================
[ 3. 알고리즘 실행 순서 (Step-by-Step) ]
- Step 1 [DP 테이블 초기화] : dp[b_trace] 형태로 배열을 만듭니다. 
                             'B의 흔적이 b_trace일 때, 가능한 A의 최소 흔적'을 저장하며, 무한대(INF)로 초기화합니다.
- Step 2 [초기 상태 설정] : 아무것도 안 훔쳤을 때 B의 흔적은 0, A의 흔적도 0이므로 dp[0] = 0 세팅으로 시작합니다.
- Step 3 [물건 순회 및 역순 업데이트] : 각 물건을 하나씩 확인하며, DP 테이블을 뒤에서부터(m-1부터 0까지) 역순으로 업데이트합니다.
- Step 4 [두 가지 선택지 계산] : 
     ① B가 훔치는 경우: B의 흔적이 늘어나므로 `dp[j + b_info]` 자리에 기존 `dp[j]` 값을 보냅니다. (A 흔적 증가 없음)
     ② A가 훔치는 경우: B의 흔적은 그대로이고 A의 흔적이 늘어나므로 `dp[j]` 자리에 `a_info`를 더해 갱신합니다.
- Step 5 [최종 결과 도출] : 모든 물건을 돌고 난 후, dp[0]부터 dp[m-1]까지의 값 중 최솟값을 찾습니다. 
                           이 최솟값이 n 미만이면 정답으로 반환하고, n 이상이거나 방법이 없다면 -1을 반환합니다.

================================================================================
[ 4. 초보자를 위한 핵심 알고리즘 개념 공부방 ]

💡 개념 1: 다이나믹 프로그래밍 (Dynamic Programming - DP)
   - 의미: 큰 문제를 한 번에 풀지 않고, 작은 문제들의 최적의 답을 구해 구석에 적어둔 뒤, 이를 조립해 큰 문제를 푸는 기법입니다.
   - 이 문제에서의 활용: '1번째 물건까지 고려했을 때 B의 흔적별 A의 최소 흔적' -> '2번째 물건까지 고려했을 때...' 
     식으로 차근차근 누적해가며 최적의 경로를 찾습니다.

💡 개념 2: 1차원 배낭 문제 공간 최적화 (Knapsack Space Optimization)
   - 의미: 원래는 `dp[물건번호][B의흔적]` 구조의 2차원 배열이 필요하지만, 테이블을 '뒤에서부터(역순)' 갱신하면 
           이전 물건의 데이터가 덮어써지는 것을 막을 수 있어 1차원 배열 하나로 메모리를 극도로 아낄 수 있습니다.
================================================================================
"""

def solution(info, n, m):
    # 가상의 큰 값(무한대)을 설정합니다. A의 흔적 최솟값을 갱신할 때 비교용으로 쓰입니다.
    INF = float('inf')
    
    # [Step 1] DP 테이블 생성: dp[b]는 'B의 흔적 총합이 b일 때, A의 최소 흔적 개수'를 뜻합니다.
    # B의 흔적은 m 미만이어야 하므로 크기는 m만큼 만듭니다. (인덱스 0 ~ m-1)
    dp = [INF] * m
    
    # [Step 2] 아무것도 훔치지 않은 시작 상태: B의 흔적이 0일 때, A의 최소 흔적도 0입니다.
    dp[0] = 0
    
    # [Step 3] 물건을 하나씩 꺼내며 탐색을 시작합니다.
    for a_info, b_info in info:
        # 1차원 DP 공간 최적화를 위해 뒤에서부터 역순(m-1부터 0까지)으로 채워나갑니다.
        # 앞으로 전진하며 채우면 '이번에 바뀐 값'이 다음 연산에 중복 적용되는 오류가 생기기 때문입니다.
        for j in range(m - 1, -1, -1):
            # 아직 만들 수 없는 유효하지 않은 흔적 조합 조합이라면 건너뜁니다.
            if dp[j] == INF:
                continue
                
            # 현재 기록된 A의 흔적 값을 백업해둡니다.
            current_a = dp[j]
            
            # [Step 4 - 선택지 ①] 현재 물건을 B가 훔치는 경우
            # B의 흔적이 j에서 (j + b_info)로 커집니다. 이 값이 m 미만일 때만 유효합니다.
            if j + b_info < m:
                # B가 흔적을 다 가져갔으므로 A의 흔적(current_a)은 그대로 유지된 채 해당 자리의 최솟값으로 갱신됩니다.
                if current_a < dp[j + b_info]:
                    dp[j + b_info] = current_a
            
            # [Step 4 - 선택지 ②] 현재 물건을 A가 훔치는 경우
            # B의 흔적은 j 그대로 유지되지만, A의 흔적은 기존 값에 a_info만큼 추가됩니다.
            dp[j] = current_a + a_info

    # [Step 5] 모든 물건을 다 배치한 후, B의 흔적이 m 미만(0 ~ m-1)인 범위 중에서 A의 최소 흔적을 찾습니다.
    min_a_trace = min(dp)
    
    # 만약 A의 최소 흔적이 n 미만이라면 완전범죄 성공! 최솟값을 반환합니다.
    # 만약 n 이상이거나, 모든 값이 INF 상태라 방법이 없다면 완전범죄 실패이므로 -1을 반환합니다.
    if min_a_trace < n:
        return min_a_trace
    else:
        return -1


# ================================================================================
