if __name__ == "__main__":
    # 입출력 예 #1 데이터 셋업
    test_info = [[1, 2], [2, 3], [2, 1]]
    test_n = 4
    test_m = 4
    
    # 알고리즘 실행
    result = solution(test_info, test_n, test_m)
    
    # 출력 검증
    print("-" * 60)
    print(f"▶️ 완전범죄를 위한 A도둑의 최소 흔적 개수: {result}")
