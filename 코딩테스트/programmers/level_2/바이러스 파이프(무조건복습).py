"""
================================================================================
[ 1. 문제 요약 (Problem Summary) ]
- n개의 배양체가 n-1개의 파이프로 연결된 트리 구조의 실험실이 있습니다.
- 파이프는 1(A), 2(B), 3(C) 총 3가지 종류가 있으며, 처음에는 모두 닫혀 있습니다.
- 특정 한 종류의 파이프를 선택해 '모두 열었다가 닫는 행동'을 최대 k번 반복할 수 있습니다.
- 파이프가 열려 있는 동안, 이미 바이러스에 감염된 배양체와 연결된 인접 배양체들이 연쇄적으로 감염됩니다.
- 핵심 규칙: 한 번 감염된 배양체는 파이프가 다시 닫혀도 치료되지 않고 계속 감염된 상태를 유지합니다.
- 목표: 최대 k번의 파이프 개폐 행동을 통해 감염시킬 수 있는 "최대 배양체 수"를 구해야 합니다.

================================================================================
[ 2. 입력값(매개변수) 의미 상세 요약 ]
- n (정수) : 사막 실험실에 존재하는 전체 배양체(노드)의 개수입니다. (2 <= n <= 100)
- infection (정수) : 제일 처음 바이러스에 감염된 상태로 시작하는 '초기 감염 배양체'의 번호입니다.
- edges (2차원 배열) : 배양체 사이를 연결하는 파이프선(간선)의 정보입니다. 
                       [x, y, type] 구조이며, x번과 y번 배양체가 type 종류의 파이프롤 연결됨을 뜻합니다.
                       (type은 1: A타입, 2: B타입, 3: C타입 총 3가지 종류가 있습니다.)
- k (정수) : 우리가 파이프를 열었다가 닫을 수 있는 '최대 행동 횟수'입니다. (1 <= k <= 10)

================================================================================
[ 3. 알고리즘 실행 순서 (Step-by-Step) ]
- Step 1 [그래프 빌드] : 주어진 파이프 정보(edges)를 양방향 트리 구조(인접 리스트)인 graph로 변환합니다.
- Step 2 [초기 상태 설정] : 최초 감염자(infection)를 감염 세트에 넣고 DFS 탐색을 시작합니다.
- Step 3 [DFS 파이프 조합 선택] : 1번, 2번, 3번 파이프 중 어떤 종류를 열지 k번의 선택 조합을 
                                 깊이 우선 탐색(DFS)으로 하나씩 결정해 나갑니다.
- Step 4 [BFS 바이러스 확산] : 파이프가 선택되면, 현재 감염된 모든 배양체들을 시작점으로 삼아 
                               너비 우선 탐색(BFS)을 돌려 열린 파이프를 타고 바이러스를 사방으로 퍼뜨립니다.
- Step 5 [최댓값 갱신 및 기저 조건] : 매 단계마다 감염자 수의 최댓값을 기록하며, 행동을 k번 모두 마쳤거나 
                                    이미 모든 배양체(n개)가 감염되었다면 탐색을 종료(Return)합니다.

================================================================================
[ 4. 초보자를 위한 핵심 알고리즘 개념 공부방 ]

💡 개념 1: DFS (Deep First Search - 깊이 우선 탐색)
   - 의미: 갈 수 있는 한 방향으로 끝까지 파고들며 탐색하는 방법입니다.
   - 이 문제에서의 활용: 최대 k번 파이프를 열 수 있으므로, 어떤 종류의 파이프를 열지 모든 순서(조합)를 
     결정하기 위해 사용합니다. [1번 열기 -> 2번 열기], [1번 열기 -> 3번 열기] 등을 끝까지 파고듭니다.

💡 개념 2: BFS (Breadth First Search - 너비 우선 탐색)
   - 의미: 시작 지점에서 가까운 곳부터 넓고 고르게 탐색하는 방법입니다. (큐/Queue 활용)
   - 이 문제에서의 활용: 특정 파이프가 열렸을 때, 현재 감염된 배양체들을 시작점으로 삼아 
     열린 파이프를 타고 바이러스가 인접한 곳으로 연쇄 확산되는 과정을 구현할 때 사용합니다.

💡 개념 3: 방문 체크 및 세트(Set) 활용
   - 의미: 한 번 감염된 배양체는 파이프가 닫혀도 계속 감염 상태를 유지하므로 중복 계산을 막기 위해 기록합니다.
   - 이 문제에서의 활용: 파이썬의 중복을 허용하지 않는 `set` 자료형을 사용하여 감염자 목록을 안전하게 관리합니다.
================================================================================
"""

from collections import deque

def solution(n, infection, edges, k):
    # [Step 1] 주어진 파이프 정보(edges)를 이용해 트리 구조의 인접 리스트(그래프)를 생성합니다.
    # graph[u] 에는 연결된 반대쪽 배양체 번호 v와 파이프 종류(pipe_type)가 (v, pipe_type) 형태로 저장됩니다.
    graph = [[] for _ in range(n + 1)]
    for x, y, pipe_type in edges:
        graph[x].append((y, pipe_type))
        graph[y].append((x, pipe_type))
        
    # 전역적으로 탐색하면서 찾아낸 '최대 감염자 수'를 저장할 변수입니다.
    max_infected_count = 0

    # [Step 4 구현] 특정 파이프 종류(open_type)를 열었을 때 바이러스를 확산시키는 BFS 함수입니다.
    def spread_virus(current_infected, open_type):
        # 현재 감염되어 있는 배양체들의 목록을 BFS 큐(Queue)에 한꺼번에 넣고 시작합니다.
        queue = deque(current_infected)
        # 이전 단계의 감염 상태를 훼손하지 않기 위해 새로운 세트(Set)로 복사하여 감염 상태를 유지합니다.
        next_infected = set(current_infected)
        
        while queue:
            curr = queue.popleft() # 현재 바이러스를 퍼뜨릴 중심 배양체를 큐에서 꺼냅니다.
            
            # 현재 배양체와 파이프로 연결된 이웃 배양체들을 하나씩 검사합니다.
            for neighbor, pipe_type in graph[curr]:
                # 만약 연결된 파이프 종류가 지금 열어준 파이프(open_type)와 일치하고, 
                # 그 이웃 배양체가 아직 감염되지 않은 뽀송뽀송한 상태라면?
                if pipe_type == open_type and neighbor not in next_infected:
                    next_infected.add(neighbor)  # 바이러스에 감염시키고 세트에 추가합니다.
                    queue.append(neighbor)       # 이 새로 감염된 배양체로부터 또 퍼질 수 있으므로 큐에 넣습니다.
                    
        return next_infected # 파이프가 열려있는 동안 최종적으로 감염된 새로운 감염자 명단을 반환합니다.

    # [Step 3 & 5 구현] 파이프를 여는 모든 조합을 시도하는 DFS 재귀 함수입니다.
    # count: 현재까지 파이프를 열었다 닫은 횟수 (행동 횟수)
    # current_infected: 현재 시점까지 감염되어 있는 배양체들의 세트(집합)
    def dfs(count, current_infected):
        nonlocal max_infected_count
        
        # [Step 5] 현재 단계까지 확보한 감염자 수로 최댓값을 매번 갱신합니다.
        max_infected_count = max(max_infected_count, len(current_infected))
        
        # 기저 조건: 파이프를 최대 횟수(k번)만큼 모두 열었거나, 이미 모든 배양체(n개)가 100% 감염되었다면 더 탈출합니다.
        if count == k or len(current_infected) == n:
            return
            
        # 우리가 선택할 수 있는 파이프 종류는 1번, 2번, 3번 총 세 가지입니다.
        for pipe_type in [1, 2, 3]:
            # 선택한 종류의 파이프를 열었을 때 확산되어 늘어난 감염자 명단을 받아옵니다. (BFS 함수 호출)
            next_infected = spread_virus(current_infected, pipe_type)
            
            # 만약 이 종류의 파이프를 열었는데도 새로 감염된 배양체가 0개라면, 
            # 소중한 행동 기회(k)를 낭비한 것이므로 이 갈래는 더 이상 탐색하지 않고 건너뜁니다. (가지치기)
            if len(next_infected) == len(current_infected):
                continue
                
            # 행동 횟수를 1 늘리고, 새로 업데이트된 감염자 명단을 들고 다음 단계로 깊숙이 파고듭니다. (DFS 재귀 호출)
            dfs(count + 1, next_infected)

    # [Step 2] 최초 감염자(infection)를 세트에 넣고, 0번 행동한 상태 초기값으로 DFS 완전 탐색을 시작합니다.
    initial_infected = {infection}
    dfs(0, initial_infected)
    
    # 모든 경우의 수를 완벽하게 탐색한 후 찾아낸 최대 감염 배양체 수를 최종 반환합니다.
    return max_infected_count


