"""
1부터 n까지의 서로 다른 정수 5개가 오름차순으로 정렬된 비밀 코드를 가지고 있음.
비밀 코드를 맞춰야함.

당신은 비밀코드를 알아내기 위해 암호 분석도구를 사용하며, m번의 시도를 할 수 있음.
각 시도마다 서로다른 5개의 정수를 입력하면, 시스템은 그 중 몇개가 비밀코드에 포함되어있는지 알려줌.
    - ex) 만약 비밀코드가 [3,5,7,9,10] 이고, 입력한 정수가 [1,2,3,4,5]라면 비밀코드에 포함된 정수는 3,5 두개이므로 시스템은 2를 응답함.

당신은 m번의 시도 후, 비밀 코드로 가능한 정수 조합의 개수를 알고 싶다.

정수 n, 입력한 정수를 담은 2차원 정수배열 q와 시스템 응답을 담은 1차원 정수배열 ans
이때, 비밀코드로 가능한 정수 조합 개수를 return


1. 💡 알고리즘 설계 (단계별 순서)
   - 1단계 [모든 후보 조합 생성]: 1부터 n까지의 정수 중 서로 다른 5개를 고르는 모든 오름차순 조합을 생성합니다.
     (제한사항에서 n이 최대 30이므로, 30개 중 5개를 고르는 조합의 수는 30C5 = 142,506개밖에 안 되어 완전 탐색이 가능합니다.)
   - 2단계 [조합 필터링]: 생성된 각각의 5개짜리 숫자 조합(candidate)이 문제에서 제공한 '모든 시도(q)'와 '응답(ans)'을 만족하는지 확인합니다.
     ① 하나의 후보 조합을 들고, 질문 리스트 q의 모든 원소와 하나씩 비교를 시작합니다.
     ② 후보 조합과 질문 q[i] 배열에 공통으로 포함된 숫자의 개수(교집합의 크기)를 구합니다.
     ③ 이 교집합의 개수가 시스템의 실제 응답인 ans[i]와 일치하는지 검사합니다.
   - 3단계 [조건 충족 카운트]: 질문 리스트 q의 모든 시도에 대해 힌트가 전부 맞아떨어지는 후보 조합인 경우에만 정답 카운트(answer)를 1 증가시킵니다.
   - 4단계 [최종 반환]: 검사가 끝난 후, 모든 조건을 만족하는 정수 조합의 총개수를 반환합니다.

2. 📝 필요 개념
   - 조합 (Combinations): 순서를 고려하지 않고 서로 다른 n개 중 r개를 선택하는 경우의 수입니다. 파이썬의 `itertools.combinations`를 사용하면 효율적입니다.
   - 교집합 (Intersection) / 세트 (Set): 두 리스트에 공통으로 들어있는 원소의 개수를 빠르게 구하기 위해 집합 자료형(`set`)의 교집합 연산(`&`)을 활용하면 코드가 간결해집니다.
"""

from itertools import combinations

def solution(n, q, ans):
    answer = 0
    
    # [코드 설명] 1부터 n까지의 숫자로 이루어진 리스트 생성
    numbers = list(range(1, n + 1))
    
    # [코드 설명] 1~n 중에서 서로 다른 5개를 고르는 모든 가능한 조합(비밀 코드 후보)을 순회
    for candidate in combinations(numbers, 5):
        # [코드 설명] 교집합 연산을 빠르게 하기 위해 후보 리스트를 set(집합) 구조로 변환
        candidate_set = set(candidate)
        
        # [코드 설명] 현재 후보 조합이 모든 시스템 힌트(q, ans)를 만족하는지 체크할 플래그 변수
        is_valid = True
        
        # [코드 설명] 문제에서 주어진 m번의 시도(q)와 시스템 응답(ans)을 하나씩 꺼내어 비교
        for i in range(len(q)):
            # [코드 설명] 시도한 5개의 정수 배열 q[i]와 현재 후보 조합의 공통 원소 개수(교집합 크기) 계산
            match_count = len(candidate_set & set(q[i]))
            
            # [코드 설명] 만약 공통 원소 개수가 시스템의 응답인 ans[i]와 다르다면, 이 후보는 탈락!
            if match_count != ans[i]:
                is_valid = False
                break  # [코드 설명] 이미 조건이 틀렸으므로 다음 시도들은 검사할 필요 없이 내부 루프 탈락
                
        # [코드 설명] m번의 모든 시도와 응답 힌트를 완벽하게 만족한 조합인 경우 정답 카운트 증가
        if is_valid:
            answer += 1
            
    # [코드 설명] 조건에 만족하는 가능한 모든 정수 조합의 총 개수 반환
    return answer


