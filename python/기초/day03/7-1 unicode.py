# 인코딩하기
"""
유니코드 문자열은 인코딩(encoding) 없이 그대로 파일에 저거나 다른 시스템으로 전송할 수 없다.
왜냐하면 실제 바이트로 어떻게 저장할지 별도로 정해야하기 때문이다.
파일에 적거나 다른 시스템으로 전송하려면 바이트(byte)문자열로 변환해야한다.
바이트 문자열=>유니코드 문자열 : 디코딩
"""

a="Life is too short"
print(type(a))

b=a.encode('utf-8')
print(b) #문자앞에 "b"가 붙었는데 해당 문자열이 바이트 문자열임을 나타냄
print(type(b))

a='한글'
#a.encode("ascii")
#print(type(a))
#한글을 아스키로는 표현할 수 없다.

b=a.encode('euc-kr')
print(b)
print(type(b))

b.decode('euc-kr')
print(b)
print(type(b))


"""
유니코드 문자열  ---encode('utf-8')--->  바이트 문자열
"한글"  <---decode('utf-8')---   b'\xed\x95\x9c\xea\xb8\x80'

유니코드: 전 세계 문자에 번호를 매긴 약속(문자 셋)
인코딩: 유니코드 문자열 → 바이트 문자열 (저장, 전송할 때)
디코딩: 바이트 문자열 → 유니코드 문자열 (읽어올 때)
"""

"""
<입출력과 인코딩>

프로그램 내부에서는 항상 유니코드 문자열을 사용하고, 외부와 데이터를 주고받을 때만 인코딩/디코딩하는 것이다. 
파일을 읽거나 네트워크를 통해 데이터를 주고받을 때 추천하는 방법은 다음과 같다.

1. 입력으로 받은 바이트 문자열은 되도록 빨리 유니코드 문자열로 디코딩한다.
2. 함수나 클래스 등에서는 유니코드 문자열만 사용한다.
3. 입력에 대한 결과를 전송하는 마지막 부분에서만 유니코드 문자열을 바이트 문자열로 인코딩해서 반환한다.

이 규칙을 지킨다면 인코딩과 관련해서 큰 어려움은 없을 것이다.
"""


