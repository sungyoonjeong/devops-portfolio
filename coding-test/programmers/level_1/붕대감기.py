# 문제 설명
# 어떤 게임에는 붕대 감기라는 기술이 있습니다.

# 붕대 감기는 t초 동안 붕대를 감으면서 1초마다 x만큼의 체력을 회복합니다. 
# t초 연속으로 붕대를 감는 데 성공한다면 y만큼의 체력을 추가로 회복합니다. 
# 게임 캐릭터에는 최대 체력이 존재해 현재 체력이 최대 체력보다 커지는 것은 불가능합니다.

# 기술을 쓰는 도중 몬스터에게 공격을 당하면 기술이 취소되고, 공격을 당하는 순간에는 체력을 회복할 수 없습니다. 
# 몬스터에게 공격당해 기술이 취소당하거나 기술이 끝나면 그 즉시 붕대 감기를 다시 사용하며, 연속 성공 시간이 0으로 초기화됩니다.

# 몬스터의 공격을 받으면 정해진 피해량만큼 현재 체력이 줄어듭니다. 
# 이때, 현재 체력이 0 이하가 되면 캐릭터가 죽으며 더 이상 체력을 회복할 수 없습니다.

# 당신은 붕대감기 기술의 정보, 캐릭터가 가진 최대 체력과 몬스터의 공격 패턴이 주어질 때 캐릭터가 끝까지 생존할 수 있는지 궁금합니다.

# 붕대 감기 기술의 시전 시간, 1초당 회복량, 추가 회복량을 담은 1차원 정수 배열 bandage와 최대 체력을 의미하는 정수 health, 
# 몬스터의 공격 시간과 피해량을 담은 2차원 정수 배열 attacks가 매개변수로 주어집니다. 모든 공격이 끝난 직후 남은 체력을 return 하도록 solution 함수를 완성해 주세요. 
# 만약 몬스터의 공격을 받고 캐릭터의 체력이 0 이하가 되어 죽는다면 -1을 return 해주세요

def solution(bandage, max_health, attacks):
    # [함수 시작] bandage 정보([시전시간t, 초당회복x, 추가회복y]), 최대 체력, 몬스터 공격 패턴을 받습니다.
    
    # 붕대 감기 기술의 세부 능력을 변수에 담아두기 쉽게 쪼갭니다.
    t, x, y = bandage  # t = 연속 성공 목표 시간, x = 1초당 회복량, y = 보너스 회복량
    
    # 캐릭터의 시작 체력을 최대 체력 상태로 가득 채우고 출발합니다.
    current_health = max_health
    
    # 연속으로 붕대를 감는 데 성공한 초(시간)를 세어줄 변수입니다. 0초부터 시작합니다.
    continuous_time = 0
    
    # 몬스터의 공격 기록(attacks)을 언제 꺼내서 매칭할지 추적하는 순서 인덱스입니다.
    attack_idx = 0
    
    # 마지막 공격이 끝나는 시간까지 게임이 진행되므로, 마지막 공격 시각을 구합니다.
    # 예: attacks가 [[1, 2], [3, 5]]라면 마지막 공격 시간인 3을 가져옵니다.
    last_attack_time = attacks[-1][0]
    
    # --------------------------------------------------------
    # [시간 시뮬레이션 단계] 게임 시작인 1초부터 마지막 공격 시간까지 1초씩 흘려보냅니다.
    # --------------------------------------------------------
    for current_time in range(1, last_attack_time + 1):
        
        # 만약 현재 시간(current_time)이 몬스터가 공격하기로 예고된 타이밍이라면 실행됩니다.
        if current_time == attacks[attack_idx][0]:
            # 몬스터가 때린 피해량(damage)을 공격 정보에서 가져옵니다.
            damage = attacks[attack_idx][1]
            
            # 현재 내 체력에서 몬스터의 피해량만큼 깎아냅니다.
            current_health -= damage
            
            # 공격을 받았으므로 지금까지 열심히 감던 붕대의 연속 성공 시간이 0으로 완전 초기화됩니다.
            continuous_time = 0
            
            # 이번 몬스터 공격 처리가 끝났으므로, 다음 공격을 감시하기 위해 인덱스를 1 늘려줍니다.
            attack_idx += 1
            
            # 만약 공격을 받아 체력이 0 이하(0 또는 음수)가 되었다면 캐릭터가 사망한 것입니다.
            if current_health <= 0:
                return -1 # 문제 요구사항에 따라 캐릭터가 죽으면 즉시 -1을 리턴하고 게임을 끝냅니다.
                
        # 몬스터의 공격이 없는 평화로운 초(시간)라면 회복 로직을 실행합니다.
        else:
            # 안전하게 붕대를 1초 동안 감았으므로 연속 성공 시간을 1초 늘려줍니다.
            continuous_time += 1
            
            # 기본적으로 1초당 회복량(x)만큼 체력을 즉시 회복시킵니다.
            current_health += x
            
            # [보너스 체크] 만약 연속 성공 시간이 기술의 시전 시간(t)에 도달했다면 보너스가 터집니다!
            if continuous_time == t:
                current_health += y # 추가 회복량(y)만큼 체력을 더 보너스로 얹어줍니다.
                continuous_time = 0 # 보너스를 받았으므로 연속 성공 시간은 다시 0으로 초기화됩니다.
                
            # 회복을 마친 후, 혹시 내 체력이 최대 체력(max_health)을 삐져나갔는지 검사합니다.
            if current_health > max_health:
                current_health = max_health # 최대 체력을 넘을 수는 없으므로 최대 체력 값으로 고정합니다.

    # --------------------------------------------------------
    # [종료 단계] 마지막 공격까지 다 버텨내고 무사히 살아남았다면 실행됩니다.
    # --------------------------------------------------------
    # 남은 최종 현재 체력을 정답으로 반환합니다.
    return current_health


# 위쪽에 solution 함수 코드가 작성되어 있어야 합니다.

# 제공해주신 4가지 공식 입출력 예시 데이터 선언
test_cases = [
    {
        "bandage": [5, 1, 5],
        "health": 30,
        "attacks": [[2, 10], [9, 15], [10, 5], [11, 5]],
        "expected": 5
    },
    {
        "bandage": [3, 2, 7],
        "health": 20,
        "attacks": [[1, 15], [5, 16], [8, 6]],
        "expected": -1
    },
    {
        "bandage": [4, 2, 7],
        "health": 20,
        "attacks": [[1, 15], [5, 16], [8, 6]],
        "expected": -1
    },
    {
        "bandage": [1, 1, 1],
        "health": 5,
        "attacks": [[1, 2], [3, 2]],
        "expected": 3
    }
]

# 반복문을 통해 모든 테스트 케이스를 순서대로 검증
for i, tc in enumerate(test_cases, 1):
    # 구현한 solution 함수 호출
    result = solution(tc["bandage"], tc["health"], tc["attacks"])
    
    print(f"--- 테스트 케이스 {i} ---")
    print(f"기술 정보(t, x, y): {tc['bandage']}, 최대 체력: {tc['health']}")
    print(f"실행 결과 (내 코드): {result}")
    print(f"기대 정답 (표의 값): {tc['expected']}")
    
    # 정답과 일치하는지 비교 및 출력
    if result == tc["expected"]:
        print("결과:        ✅ 통과 (PASS)")
    else:
        print("결과:        ❌ 실패 (FAIL)")
    print()
