"""
1 ~ n의 번호가 있는 택배 상자가 창고에 있습니다. 당신은 택배 상자들을 다음과 같이 정리했습니다.

왼쪽에서 오른쪽으로 가면서 1번 상자부터 번호 순서대로 택배 상자를 한 개씩 놓습니다. 
가로로 택배 상자를 w개 놓았다면 이번에는 오른쪽에서 왼쪽으로 가면서 그 위층에 택배 상자를 한 개씩 놓습니다. 
그 층에 상자를 w개 놓아 가장 왼쪽으로 돌아왔다면 또다시 왼쪽에서 오른쪽으로 가면서 그 위층에 상자를 놓습니다. 
이러한 방식으로 n개의 택배 상자를 모두 놓을 때까지 한 층에 w개씩 상자를 쌓습니다.

다음 날 손님은 자신의 택배를 찾으러 창고에 왔습니다. 
당신은 손님이 자신의 택배 상자 번호를 말하면 해당 택배 상자를 꺼내줍니다. 
택배 상자 A를 꺼내려면 먼저 A 위에 있는 다른 모든 상자를 꺼내야 A를 꺼낼 수 있습니다. 
예를 들어, 위 그림에서 8번 상자를 꺼내려면 먼저 20번, 17번 상자를 꺼내야 합니다.

당신은 꺼내려는 상자 번호가 주어졌을 때, 꺼내려는 상자를 포함해 총 몇 개의 택배 상자를 꺼내야 하는지 알고 싶습니다.

창고에 있는 택배 상자의 개수를 나타내는 정수 n, 가로로 놓는 상자의 개수를 나타내는 정수 w와 꺼내려는 택배 상자의 번호를 나타내는 정수 num이 매개변수로 주어집니다. 
이때, 꺼내야 하는 상자의 총개수를 return 하도록 solution 함수를 완성해 주세요.
"""

def solution(n, w, num):
    answer = 0
    
    # =========================================================================
    # 1단계: 전체 택배 상자들을 지그재그 구조의 창고(2D 격자)에 시뮬레이션 배치하기
    # =========================================================================
    storage = []       # 택배 상자들이 층별로 담길 창고(2차원 리스트) 레이아웃이에요.
    current_box = 1    # 1번 상자부터 시작해서 차곡차곡 숫자를 올려가며 쌓을 거예요.
    floor = 0          # 바닥(0번째 층)부터 시작해서 한 층씩 위로 올라갑니다.
    
    # 모든 상자(n개)를 창고에 전부 채울 때까지 반복해서 쌓아 올려요.
    while current_box <= n:
        row = []  # 현재 층에 가로 w개만큼 상자를 담을 임시 리스트(한 줄)를 만들어요.
        
        # 가로 칸수(w)만큼 상자를 순서대로 일렬 배치합니다.
        for _ in range(w):
            if current_box <= n:
                row.append(current_box)   # 상자가 아직 남아있다면 실제 번호를 올립니다.
                current_box += 1          # 다음 상자 번호로 숫자를 1 늘려줍니다.
            else:
                row.append(0)             # 전체 개수 n을 넘어가서 상자가 비어있는 칸은 0으로 채워요.
                
        # 🚨 [핵심 규칙]: 홀수 번째 층(1층, 3층, 5층...)은 오른쪽에서 왼쪽으로 거꾸로 채워야 해요.
        if floor % 2 == 1:
            row.reverse()  # 파이썬 내장 함수를 사용해 한 줄의 정렬 순서를 거꾸로 뒤집어 줍니다.
            
        storage.append(row)  # 지그재그 보정이 끝난 한 줄을 창고(storage)에 최종 적재해요.
        floor += 1           # 다음 윗층으로 올라가기 위해 층수를 1 증가시킵니다.

    # =========================================================================
    # 2단계: 손님이 요청한 num 상자의 물리적 위치(행, 열) 수색하기
    # =========================================================================
    target_row = -1  # 우리가 꺼내야 할 상자가 위치한 층(행 인덱스) 번호가 저장될 변수예요.
    target_col = -1  # 우리가 꺼내야 할 상자가 위치한 열(세로 라인 인덱스) 번호가 저장될 변수예요.
    
    # 창고 전체를 행(r)과 열(c)로 2중 순회하면서 num 상자의 주소를 찾습니다.
    for r in range(len(storage)):
        for c in range(w):
            if storage[r][c] == num:
                target_row = r  # 상자를 찾았으므로 행 주소를 박제합니다.
                target_col = c  # 세로로 똑바로 위에 있는 상자들만 꺼내야 하므로 열 주소를 박제합니다.
                break

    # =========================================================================
    # 3단계: num 상자의 주소부터 수직(세로 열) 위쪽으로 가로막고 있는 진짜 상자 개수 세기
    # =========================================================================
    # num이 있는 층(target_row)부터 창고의 맨 꼭대기 층까지 수직으로 올라가며 조사해요.
    for r in range(target_row, len(storage)):
        # 같은 세로 라인(target_col)을 열었을 때, 0이 아닌 실제 택배 상자가 들어있다면
        if storage[r][target_col] != 0:
            answer += 1  # 꺼내야 하므로 정답 카운트를 1 증가시킵니다.
            
    return answer  # 수직 위로 쌓인 모든 상자의 총개수를 반환하며 완결됩니다.



# 🧪 테스트 케이스 1 (이미지 지문 속 그림 예시)
# 총 상자 22개, 가로 6개, 8번 상자를 꺼내는 상황
n1, w1, num1 = 22, 6, 8
result1 = solution(n1, w1, num1)

print(f"테스트 1 결과: {result1} (정답: 3)")
if result1 == 3:
    print("➡️ 테스트 1번 통과! 🎉 (8번 위로 17번, 20번이 가로막고 있어요)")
else:
    print("➡️ 테스트 1번 실패... 😢")

print("-" * 50)

