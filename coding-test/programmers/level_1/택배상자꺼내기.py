# 총 상자 13개, 가로 3개, 6번 상자를 꺼내는 상황
n2, w2, num2 = 13, 3, 6
result2 = solution(n2, w2, num2)

print(f"테스트 2 결과: {result2} (정답: 4)")
if result2 == 4:
    print("➡️ 테스트 2번 통과! 🎉 (6번 위로 쌓인 4개의 상자를 완벽히 체크했어요)")
else:
    print("➡️ 테스트 2번 실패... 😢")
