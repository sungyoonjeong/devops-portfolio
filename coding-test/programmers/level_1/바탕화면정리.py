'''
문제 설명
코딩테스트를 준비하는 머쓱이는 프로그래머스에서 문제를 풀고 나중에 다시 코드를 보면서 공부하려고 
작성한 코드를 컴퓨터 바탕화면에 아무 위치에나 저장해 둡니다. 
저장한 코드가 많아지면서 머쓱이는 본인의 컴퓨터 바탕화면이 너무 지저분하다고 생각했습니다. 
프로그래머스에서 작성했던 코드는 그 문제에 가서 다시 볼 수 있기 때문에 저장해 둔 파일들을 전부 
삭제하기로 했습니다.

컴퓨터 바탕화면은 각 칸이 정사각형인 격자판입니다. 
이때 컴퓨터 바탕화면의 상태를 나타낸 문자열 배열 wallpaper가 주어집니다. 
파일들은 바탕화면의 격자칸에 위치하고 바탕화면의 격자점들은 바탕화면의 가장 왼쪽 위를 (0, 0)으로 시작해 
(세로 좌표, 가로 좌표)로 표현합니다. 빈칸은 ".", 파일이 있는 칸은 "#"의 값을 가집니다. 
드래그를 하면 파일들을 선택할 수 있고, 선택된 파일들을 삭제할 수 있습니다. 머쓱이는 최소한의 이동거리를 
갖는 한 번의 드래그로 모든 파일을 선택해서 한 번에 지우려고 하며 드래그로 파일들을 선택하는 방법은 다음과 
같습니다.

드래그는 바탕화면의 격자점 S(lux, luy)를 마우스 왼쪽 버튼으로 클릭한 상태로 
격자점 E(rdx, rdy)로 이동한 뒤 마우스 왼쪽 버튼을 떼는 행동입니다. 
이때, "점 S에서 점 E로 드래그한다"고 표현하고 점 S와 점 E를 각각 드래그의 시작점, 끝점이라고 표현합니다.

점 S(lux, luy)에서 점 E(rdx, rdy)로 드래그를 할 때, "드래그 한 거리"는 
|rdx - lux| + |rdy - luy|로 정의합니다.

점 S에서 점 E로 드래그를 하면 바탕화면에서 두 격자점을 각각 왼쪽 위, 오른쪽 
아래로 하는 직사각형 내부에 있는 모든 파일이 선택됩니다.
'''

'''
step1) 드래그의 원리 이해
    - 격자판에서 파일 (#)의 위치를 (행,열), 즉, (r,c)라고 할때, 하나의 파일을 감싸는 드래그 영역은 아래와 같다
        - 시작점(lux,luy): 파일의 왼쪽 위 꼭짓점=>(r,c)
        - 끝점(rdx,rdy): 파일의 오른쪽 아래 꼭짓점=>(r+1,c+1)

step2) 모든 파일을 포함하는 최소/최대 좌표 찾기
    - 여러개의 파일이 있을때, 이 파일들을 단 한번의 드래그로 모두 감싸려면 모든 파일의 위치를 고려한 
    최소/최대 경계를 찾아야함
        - lux(시작행): 모든 파일의 r좌표중 최솟값
        - luy(시작열): 모든 파일의 c좌표중 최솟값
        - rdx(끝행): 모든 파일의 r+1좌표중 최대값
        - rdy(끝열): 모든 파일의 c+1좌표중 최대값

step3) 초기값 설정 및 탐색 계획
    - 최솟값을 구해야하는 lux와 luy는 바탕화면의 최대크기(50)보다 큰값으로 초기화
    - 최대값을 구해야하는 rdx와 rdy는 가장 작은 값인 0으로 초기화
    - 바탕화면(wallpaper)을 반복문으로 돌면서 #을 찾을 때마다 최솟값과 최대값을 갱신.
'''

def solution(wallpaper):
    # 바탕화면의 세로 크기(행 개수)와 가로 크기(열 개수)를 구합니다.
    H = len(wallpaper)
    W = len(wallpaper[0])
    
    # 시작점(lux, luy)은 최솟값을 찾아야 하므로 각각 가질 수 있는 가장 큰 값으로 초기화합니다.
    lux, luy = H, W
    # 끝점(rdx, rdy)은 최대값을 찾아야 하므로 가장 작은 값인 0으로 초기화합니다.
    rdx, rdy = 0, 0
    
    # 바탕화면의 모든 칸을 행(r) 단위로 먼저 탐색합니다.
    for r in range(H):
        # 각 행 내부에서 열(c) 단위로 칸을 하나씩 탐색합니다.
        for c in range(W):
            # 만약 현재 위치에 파일('#')이 존재한다면 경계값을 갱신합니다.
            if wallpaper[r][c] == '#':
                lux = min(lux, r)      # 파일이 있는 가장 위쪽 행 번호로 갱신합니다.
                luy = min(luy, c)      # 파일이 있는 가장 왼쪽 열 번호로 갱신합니다.
                rdx = max(rdx, r + 1)  # 파일의 아래쪽 모서리를 포함해야 하므로 r + 1의 최대값으로 갱신합니다.
                rdy = max(rdy, c + 1)  # 파일의 오른쪽 모서리를 포함해야 하므로 c + 1의 최대값으로 갱신합니다.
                
    # 계산된 최소 직사각형의 시작점과 끝점 좌표를 리스트에 담아 반환합니다.
    return [lux, luy, rdx, rdy]


