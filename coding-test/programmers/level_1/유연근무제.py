"""
프로그래머스 사이트를 운영하는 그렙에서는 재택근무와 함께 출근 희망 시각을 자유롭게 정하는 유연근무제를 시행하고 있습니다. 
제도 정착을 위해 오늘부터 일주일 동안 각자 설정한 출근 희망 시각에 늦지 않고 출근한 직원들에게 상품을 주는 이벤트를 진행하려고 합니다.

직원들은 일주일동안 자신이 설정한 출근 희망 시각 + 10분까지 어플로 출근해야 합니다. 
예를 들어 출근 희망 시각이 9시 58분인 직원은 10시 8분까지 출근해야 합니다. 
단, 토요일, 일요일의 출근 시각은 이벤트에 영향을 끼치지 않습니다. 
직원들은 매일 한 번씩만 어플로 출근하고, 모든 시각은 시에 100을 곱하고 분을 더한 정수로 표현됩니다. 
예를 들어 10시 13분은 1013이 되고 9시 58분은 958이 됩니다.

당신은 직원들이 설정한 출근 희망 시각과 실제로 출근한 기록을 바탕으로 상품을 받을 직원이 몇 명인지 알고 싶습니다.

직원 n명이 설정한 출근 희망 시각을 담은 1차원 정수 배열 schedules, 직원들이 일주일 동안 출근한 시각을 담은 2차원 정수 배열 timelogs, 
이벤트를 시작한 요일을 의미하는 정수 startday가 매개변수로 주어집니다. 이때 상품을 받을 직원의 수를 return 하도록 solution 함수를 완성해주세요.
"""



def solution(schedules, timelogs, startday):
    answer = 0  # 상품을 받을 수 있는 최종 우수 직원의 수를 저장할 변수예요.
    
    # -------------------------------------------------------------------------
    # 1단계: 직원 한 명씩 순서대로 일주일 치 출근 기록(timelogs) 검사하기
    # -------------------------------------------------------------------------
    # len(schedules)는 전체 직원의 수예요. i는 0번 직원부터 마지막 직원까지의 번호가 됩니다.
    for i in range(len(schedules)):
        
        # [시간 변환 공식]: 희망 출근 시각에 10분을 더한 '지각 커트라인 시각'을 구해요.
        # 시(hour)와 분(minute)을 분리한 뒤, 10분을 더하고 다시 60분이 넘으면 시를 1 올려주는 정석 연산이에요.
        hope_time = schedules[i]
        hour = hope_time // 100         # 100으로 나눈 몫을 구하면 '시'가 나와요. (예: 958 // 100 = 9)
        minute = hope_time % 100        # 100으로 나눈 나머지를 구하면 '분'이 나와요. (예: 958 % 100 = 58)
        
        minute += 10                    # 출근 희망 시각에 10분의 보너스 시간을 더해줍니다.
        if minute >= 60:                # 만약 분이 60분 이상이 되었다면 (예: 68분)
            hour += 1                   # 시를 1시간 올려주고 (9시 ➡️ 10시)
            minute -= 60                # 분에서 60분을 빼줍니다 (68분 ➡️ 8분)
            
        limit_time = hour * 100 + minute  # 가공 완료된 최종 지각 커트라인 시각이에요. (예: 1008)

        # 이 직원이 일주일 동안 단 한 번이라도 지각했는지 체크할 깃발 변수예요.
        is_perfect = True 
        
        # -------------------------------------------------------------------------
        # 2단계: 해당 직원의 월~일(7일간) 실시간 출근 기록 확인 및 요일 계산
        # -------------------------------------------------------------------------
        # j는 0부터 6까지 돌며, timelogs[i][j]를 통해 이 직원의 1번째 날부터 7번째 날까지의 기록을 꺼내요.
        for j in range(7):
            # [요일 계산 규칙]: 시작 요일(startday)에 지난 일수(j)를 더해서 오늘이 무슨 요일인지 구해요.
            # 1=월, 2=화, 3=수, 4=목, 5=금, 6=토, 7=일 이에요.
            current_day = startday + j
            
            # 만약 요일 번호가 7을 넘어가면 (예: 일요일(7) 다음 날인 8이 되면) 다시 월요일(1)로 순환시켜 줍니다.
            if current_day > 7:
                current_day -= 7
                
            # [문제 예외 조건]: 토요일(6)이거나 일요일(7)인 날은 이벤트에 영향을 주지 않으므로 무조건 패스합니다!
            if current_day == 6 or current_day == 7:
                continue  # 아래 지각 체크 코드를 무시하고 다음 날 기록 검사로 바로 넘어갑니다.
                
            # [지각 체크]: 실제 출근 시각이 커트라인 시각(limit_time)을 단 1분이라도 초과했다면 지각이에요!
            if timelogs[i][j] > limit_time:
                is_perfect = False  # 지각이 확인되었으므로 우수 직원 탈락 마킹을 해요.
                break               # 이미 탈락했으니 이 직원의 남은 요일 기록은 더 볼 필요 없이 탈출합니다.
                
        # 7일 동안 주말 제외하고 단 한 번도 지각하지 않아 깃발이 True 상태로 살아남았다면 합격!
        if is_perfect:
            answer += 1  # 상품을 받을 직원 수 카운트를 1 올려줍니다.
            
    return answer  # 최종 상품을 받을 직원의 총 명수를 반환하며 완료됩니다.



# 🧪 기본 제공 예시 테스트
schedules_test = [900, 1000, 1100]
timelogs_test = [
    [905, 909, 914, 921, 904, 1022, 1111],  # 1번째 직원 (4번째 날 921분에 출근해서 지각!)
    [1005, 1004, 1001, 1003, 1002, 1011, 1022],  # 2번째 직원 (주말 제외하고 모두 커트라인 1010 이내 출근 성공! ⭕)
    [1101, 1102, 1103, 1110, 1109, 1122, 1133]   # 3번째 직원 (모든 평일 커트라인 1110 이내 출근 성공! ⭕)
]
startday_test = 1  # 월요일부터 시작하는 일주일

result = solution(schedules_test, timelogs_test, startday_test)
print("테스트 결과:", result, "(정답: 2)")
