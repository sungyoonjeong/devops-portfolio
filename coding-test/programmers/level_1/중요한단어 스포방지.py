"""
카카오톡은 메시지의 일부를 가려두었다가, 클릭했을 때만 공개되는 스포 방지 기능을 제공합니다. 이 기능을 활용하면 중요한 정보를 가리고 보낼 수 있습니다.

무지는 이 기능을 이용해 하나의 메시지 곳곳에 스포 방지 기능을 적용해 당신에게 보냈습니다. 당신은 메시지 시작부터 왼쪽 → 오른쪽 순서로 스포 방지 구간을 하나씩 클릭해 공개되는 단어들 중, 중요한 단어가 몇 개인지 확인하려 합니다.

단어 및 중요한 단어 규칙

단어는 공백으로 구분되며, 알파벳 소문자와 숫자로만 구성된 연속된 문자열입니다.
단어를 구성하는 문자들의 인덱스 중 하나 이상이 스포 방지 구간에 포함될 경우, 해당 단어는 스포일러 방지 단어로 간주합니다. 즉, 단어 내 일부 문자에만 스포일러 방지 기능이 적용되더라도, 해당 단어 전체를 스포일러 방지 단어로 간주합니다.
한 단어가 여러 개의 스포 방지 구간에 걸쳐 있을 수 있으며, 하나의 스포 방지 구간에 여러 단어가 포함될 수 있습니다.
스포 방지 구간을 클릭해 단어의 모든 문자가 공개되었을 때, 그 단어가 아래 조건을 모두 만족하면 중요한 단어입니다.
스포 방지 단어여야 합니다.
메시지의 스포 방지 구간이 아닌 구간(= 어떤 스포 방지 구간에도 속하지 않는 모든 구간: 각 구간의 앞·사이·뒤 포함)에 등장한 적이 없어야 합니다.
이전에 공개된 스포 방지 단어와 중복되지 않아야 합니다.
여러 단어가 동시에 공개된 경우, 왼쪽부터 순서대로 하나씩 중요한 단어인지 판단합니다.
무지가 당신에게 보내온 메시지를 나타내는 문자열 message와 스포 방지가 적용된 구간을 나타내는 2차원 정수 배열 spoiler_ranges가 매개변수로 주어질 때, 스포 방지 단어 중 중요한 단어의 수를 return 하도록 solution 함수를 완성해 주세요.
"""
"""
이 문제는 카카오톡의 스포 방지기능(클릭 시 해제)을 시뮬레이션하여, 클릭할때마다 공개되는 단어 중 '중요한 단어'의 개수를 구하는 문제입니다.

풀이 로직
1. 단어 분리 및 인덱스 파악: message 문자열을 공백기준으로 단어로 나눕니다. 이때 각 단어가 원본 메시지에서 차지하는 시작 인덱스와 끝 인덱스를 기록해둠
2. 스포 방지 단어 분류 : 각 단어의 인덱스 범위가 spoiler_ranges 중 하나라도 겹치면 그 단어는 '스포 방지 단어'가 됨. 스포 방지 단어가 아닌 일반 단어들은 미리 등장 횟수를 카운트해 둠
3. 구간 클릭 시뮬레이션 : spoiler_ranges를 순서대로 하나씩 확인하며 다음을 수행함
    1) 현재 클릭한 스포 방지 구간에 완전히 포함되어 모든 글자가 공개되는 단어를 찾음
    2) 여러 단어가 동시에 공개되면 왼쪽(인덱스가 작은 순)부터 처리
    3) 해당 단어가 다음 중요한 단어 조건을 모두 만족하는지 확인
        1. 스포 방지 단어여야함
        2. 메시지의 스포 방지 구간이 아닌 구간(일반 구간)에 등장한 적이 없어야함
        3. 이전에 공개된 스포 방지 단어들과 중복되지 않아야함(처음 공개되는 단어여야함)
    4) 조건을 만족하면 정답 카운트를 1증가시키고, '이미 공개된 단어'목록에 추가
"""
def solution(message, spoiler_ranges):
    answer = 0  # 조건에 맞는 최종 '중요한 단어'의 개수를 차곡차곡 누적할 정답 변수예요.
    n = len(message)  # 입력받은 전체 메시지 문자열의 총 글자 수(길이)를 미리 구해서 변수에 저장해요.
    
    # -------------------------------------------------------------------------
    # 0단계: 메시지의 각 글자 위치(인덱스)가 어떤 스포일러 상자들에 가려져 있는지 사전 마킹하기
    # -------------------------------------------------------------------------
    # 메시지 글자 수만큼 빈 세트({})를 원소로 가지는 리스트를 생성해요. (예: cover = {0, 3} 이면 0번, 3번 상자가 씌워짐)
    cover = [set() for _ in range(n)]
    """
    문법 하나씩 뜯어보기
    set() : 파이썬의 '세트(집합)' 자료형입니다. 중복을 허용하지 않고, 데이터를 집어넣기 아주 편한 주머니 형태입니다. 
    처음에는 아무것도 안 든 빈 주머니 상태인 set()으로 생성됩니다.
    for _ in range(n) : 메시지의 전체 글자 수(n)만큼 반복하겠다는 뜻입니다. 
    (변수 이름으로 _를 쓴 것은 반복 횟수만 채우고 변수 자체는 안 쓰겠다는 파이썬 관례입니다.)
    [ ... ] : 이 전체를 대괄호로 묶었기 때문에, "빈 세트 주머니 n개를 일렬로 연결한 리스트를 만들겠다"는 뜻이 됩니다.
    """
    
    # 출제자가 제공한 스포일러 구간 목록(spoiler_ranges)을 인덱스 번호(idx)와 함께 하나씩 꺼내어 반복문을 돌려요.
    for idx in range(len(spoiler_ranges)):
        l, r = spoiler_ranges[idx]  # 현재 꺼낸 스포일러 상자의 왼쪽 시작 인덱스(l)와 오른쪽 끝 인덱스(r)를 분해해요.
        
        # 현재 상자가 덮고 있는 범위인 l번 칸부터 r번 칸까지 한 칸씩 이동하며 글자 자리를 확인해요.
        for i in range(l, r + 1):
            cover[i].add(idx)  # 해당 글자 위치(인덱스)의 세트에 현재 스포일러 상자의 식별 번호(idx)를 쏙 집어넣어요.

    # =========================================================================
    # 1단계: 전체 메시지 문자열을 공백 기준으로 쪼개며 단어의 물리적 위치(시작/끝 인덱스) 기록하기
    # =========================================================================
    words_info = []  # 파싱을 완료한 단어들의 개별 정보(딕셔너리)들을 안전하게 담아둘 보관함 리스트를 만들어요.
    i = 0  # 문자열의 맨 첫 글자(인덱스 0)부터 순서대로 훑고 지나가기 위한 포인터 인덱스 변수를 선언해요.
    
    # 포인터 i가 메시지의 전체 글자 수 n에 도달하여 문장이 끝날 때까지 반복문을 실행해요.
    while i < n:
        if message[i] == ' ':  # 현재 포인터가 가리키는 칸의 문자가 순수한 공백(띄어쓰기)인 경우예요.
            i += 1             # 단어 글자가 아니므로 포인터를 그냥 한 칸 오른쪽으로 건너뛰게 만들어요.
            continue           # 아래쪽 파싱 코드를 수행하지 않고 즉시 while 반복문의 조건문으로 되돌아갑니다.
            
        start = i              # 공백이 아닌 진짜 단어 글자를 최초로 만났으므로, 이곳이 현재 단어의 '물리적 시작 위치'가 돼요.
        
        # 글자가 공백 없이 계속 이어지는 동안(문장의 끝에 도달하지 않고 공백 문자가 아닐 때까지) 계속 반복해요.
        while i < n and message[i] != ' ':  
            i += 1                          # 단어의 끝을 찾아내기 위해 포인터 변수 i를 계속해서 한 칸씩 오른쪽으로 전진시켜요.
            
        end = i - 1            # 위 while 반복문이 공백을 만나 멈춘 직전 칸(i-1)이 이 단어의 실제 '물리적 끝 위치'가 됩니다.
        word = message[start:i]  # 파이썬 슬라이싱 문법을 사용하여 구한 시작점부터 끝점 직전까지의 순수한 단어 텍스트를 잘라내요.
        
        # 가공 완료된 단어의 알짜배기 정보들을 구조화된 딕셔너리로 묶어서 1단계 보관함 리스트에 하나씩 추가해요.
        words_info.append({'word': word, 'start': start, 'end': end})
    
    # =========================================================================
    # 2단계: 각 단어가 가려진 상자들을 종합하여 스포 단어 여부 및 '완전 해제 타이밍' 미리 계산하기
    # =========================================================================
    pure_normal_words = set()  # 단 한 글자도 스포일러 구역에 가려지지 않고 완전 밖으로 노출되었던 일반 단어 글자들을 모을 주머니예요.
    
    # 1단계 보관함에 들어있는 모든 단어 정보들을 하나씩 차례대로 꺼내면서 조건 검사를 실시해요.
    for info in words_info:
        box_ids = set()  # 이 단어가 구성하는 글자들이 덮여있는 스포일러 상자들의 식별 번호를 중복 없이 보관할 세트 주머니예요.
        
        # 단어가 시작하는 인덱스 칸부터 끝나는 인덱스 칸까지 한 자 한 자 이동하며 0단계의 마킹 상태를 전수조사해요.
        for pos in range(info['start'], info['end'] + 1):
            box_ids |= cover[pos]  # 각 글자 칸에 씌워져 있던 스포일러 상자 번호 세트들을 비트 합집합 연산(|=)으로 전부 묶어내요.
            
        # 단어를 가리고 있는 상자 번호의 개수가 0개보다 많다면, 조금이라도 가려진 상태이므로 '스포 방지 단어'로 취급해요.
        info['is_spoiler'] = len(box_ids) > 0
        
        # ⭐️ [이 문제의 치명적인 해결 열쇠]: 단어 전체가 완전히 대중에게 노출되는 시점(상자 클릭 타이밍)을 구해요.
        # 단어를 가리고 있는 상자 번호들 중 '가장 큰 번호(max)'의 상자가 최종 클릭되어 열리는 순간 비로소 봉인이 완벽히 풀려요!
        # 만약 애초에 스포일러에 가려지지 않은 일반 단어라면 해제 타이밍 값을 상치 않는 값인 -1로 설정해 둡니다.
        info['reveal_time'] = max(box_ids) if info['is_spoiler'] else -1
        
        # [💥 문제의 핵심 금지어 조건]: 어떤 스포일러 구역과도 단 한 칸도 안 겹친 완벽한 '순수 일반 단어'인 경우예요.
        if not info['is_spoiler']:
            pure_normal_words.add(info['word'])  # 이 단어의 글자 내용(텍스트)을 영구적인 중요한 단어 탈락 목록(금지어)에 박제합니다.

    # =========================================================================
    # 3단계: 출제자가 준 순서대로 스포 구간을 하나씩 클릭하며 '중요한 단어' 최종 수확하기
    # =========================================================================
    opened_spoiler_words = set()  # 이미 이전에 점수를 인정받아 화면에 공개 완료된 스포 단어의 글자(텍스트)를 저장할 중복 방지 주머니예요.
    
    # 스포일러 구간의 총 개수(길이)만큼 클릭 순서(t = 0, 1, 2, 3...)를 올리며 실시간으로 클릭 이벤트를 시뮬레이션해요.
    for t in range(len(spoiler_ranges)):
        currently_revealed = []  # 이번 t번째 클릭 차례에 맞춰 '모든 문자가 완벽하게 잠금 해제되는 단어들'을 담을 임시 리스트예요.
        
        # 전체 단어 목록을 쭉 순회하면서 이번 타이밍에 정확하게 봉인이 해제되는 대상을 찾아내요.
        for info in words_info:
            # 해당 단어가 스포 단어가 맞고, 그 단어의 최종 해제 타이밍(reveal_time)이 현재 클릭 차례(t)와 완벽히 일치한다면
            if info['is_spoiler'] and info['reveal_time'] == t:
                currently_revealed.append(info)  # 이번 차례에 화면에 짠 하고 완전히 튀어나올 단어로 목록에 추가해요.
        
        # words_info 배열 자체가 애초에 문장 왼쪽부터 순서대로 파싱되었기 때문에 정렬 없이도 자동으로 왼쪽 단어부터 검사하게 돼요.
        for info in currently_revealed:
            word = info['word']  # 검사 대상 단어의 실제 텍스트 내용을 변수에 담아와요.
            
            # [조건 검사 1]: 문자열 전체 문맥 속에서 단 한 번이라도 완벽히 노출되었던 순수 일반 단어(금지 목록)에 속하면 탈락시킵니다.
            if word in pure_normal_words:
                continue  # 조건에 어긋나므로 아래 점수 반영 코드를 무시하고 다음 단어 검사로 건너뜁니다.
                
            # [조건 검사 2]: 이미 이전 회차 클릭 때 중요한 단어로 합격 판정을 받아 점수를 땄던 중복 글자 내용이라면 탈락시킵니다.
            if word in opened_spoiler_words:
                continue  # 중복 단어이므로 아래 점수 반영 코드를 무시하고 다음 단어 검사로 건너뜁니다.
                
            # 위의 모든 제약 필터링 조건을 명쾌하게 돌파했다면 드디어 최종 '중요한 단어' 요건 충족으로 합격입니다!
            answer += 1  # 정답 카운트 점수를 기분 좋게 1점 증가시킵니다.
            opened_spoiler_words.add(word)  # 다음 클릭 회차 때 동일한 글자가 나올 시 중복 컷트하기 위해 획득 주머니에 저장해 둡니다.
            
    return answer  # 모든 시뮬레이션 루프가 무사히 완결된 후, 최종 누적 계산된 정답의 총 점수를 반환하며 함수가 끝납니다.


# 🧪 테스트 케이스 1
msg1 = "hello world python"
ranges1 = [[6, 10]] # "world" 구간이 스포 방지 구간
print("테스트 1 결과:", solution(msg1, ranges1)) 
# 예상 결과: 1 ("world"는 스포 단어이고, 일반 구간에 나온 적 없으므로 중요한 단어)

# 🧪 테스트 케이스 2 (중복 및 일반 구간 등장 케이스)
msg2 = "test text test"
ranges2 = [[0, 3], [10, 13]] # 첫 번째 "test"와 두 번째 "test" 모두 스포 구간
print("테스트 2 결과:", solution(msg2, ranges2))
# 예상 결과: 1 (첫 번째 test가 공개될 때 +1 되지만, 두 번째 test는 중복이라 점수 인정 안 됨)

# 🧪 테스트 케이스 3 (이미지 예시 1)
msg3 = "here is muzi here is a secret message"
ranges3 = [[0, 3], [23, 28]]
print("테스트 3 결과:", solution(msg3, ranges3)) 
# 예상 결과: 1 ("here"는 일반 구간에 다시 등장하므로 제외, "secret"만 중요한 단어로 인정)

