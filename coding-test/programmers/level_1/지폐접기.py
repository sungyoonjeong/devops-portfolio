# 테스트 케이스 데이터 선언 (보내주신 두 가지 예시)
test_cases = [
    {
        "wallet": [30, 15],
        "bill": [26, 17],
        "expected": 1
    },
    {
        "wallet": [50, 50],
        "bill": [100, 241],
        "expected": 4
    }
]

# 반복문을 돌며 작성한 함수가 정답을 맞히는지 검증
for i, tc in enumerate(test_cases, 1):
    # 우리가 만든 함수에 wallet과 bill을 넣고 실행 (※ 주의: 리스트 원본 보존을 위해 복사본 전달)
    result = solution(tc["wallet"], tc["bill"][:])
    
    print(f"--- 테스트 케이스 {i} ---")
    print(f"지갑 크기: {tc['wallet']}, 지폐 크기: {tc['bill']}")
    print(f"실행 결과 (내 코드): {result}")
    print(f"기대 정답 (표의 값): {tc['expected']}")
    
    if result == tc["expected"]:
        print("결과:        ✅ 통과 (PASS)")
    else:
        print("결과:        ❌ 실패 (FAIL)")
    print()
