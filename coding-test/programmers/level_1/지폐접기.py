# 문제 설명
# 민수는 다양한 지폐를 수집하는 취미를 가지고 있습니다. 
# 지폐마다 크기가 달라 지갑에 넣으려면 여러 번 접어서 넣어야 합니다. 
# 예를 들어 지갑의 크기가 30 * 15이고 지폐의 크기가 26 * 17이라면 한번 반으로 접어 13 * 17 크기로 만든 뒤 90도 돌려서 지갑에 넣을 수 있습니다. 
# 지폐를 접을 때는 다음과 같은 규칙을 지킵니다.

# 지폐를 접을 때는 항상 길이가 긴 쪽을 반으로 접습니다.
# 접기 전 길이가 홀수였다면 접은 후 소수점 이하는 버립니다.
# 접힌 지폐를 그대로 또는 90도 돌려서 지갑에 넣을 수 있다면 그만 접습니다.
# 지갑의 가로, 세로 크기를 담은 정수 리스트 wallet과 지폐의 가로, 세로 크기를 담은 정수 리스트 bill가 주어질 때, 
# 지갑에 넣기 위해서 지폐를 최소 몇 번 접어야 하는지 return하도록 solution함수를 완성해 주세요.

# 지폐를 지갑에 넣기 위해 접어야 하는 최소 횟수를 구하는 과정은 다음과 같습니다.

# 1. 지폐를 접은 횟수를 저장할 정수 변수 answer를 만들고 0을 저장합니다.
# 2. 반복문을 이용해 bill의 작은 값이 wallet의 작은 값 보다 크거나 bill의 큰 값이 wallet의 큰 값 보다 큰 동안 아래 과정을 반복합니다.
#     2-1. bill[0]이 bill[1]보다 크다면
#         bill[0]을 2로 나누고 나머지는 버립니다.
#     2-2. 그렇지 않다면
#         bill[1]을 2로 나누고 나머지는 버립니다.
#     2-3. answer을 1 증가시킵니다.
# 3. answer을 return합니다.

def solution(wallet, bill):
    # [지갑 wallet, 지폐 bill 두 개의 리스트(예: [30, 15], [26, 17])를 입력받으며 함수가 시작됩니다.]
    
    answer = 0
    # 지폐를 총 몇 번 접었는지 횟수를 세어줄 저장 공간(변수)을 만들고, 시작할 때 0을 넣어둡니다.

    while min(bill) > min(wallet) or max(bill) > max(wallet):
        # [반복문 시작] 지폐가 지갑에 완전히 들어갈 수 "없는" 동안 계속 반복합니다.
        # min(bill) > min(wallet) : 지폐의 짧은 쪽이 지갑의 짧은 쪽보다 크거나,
        # or max(bill) > max(wallet) : 지폐의 긴 쪽이 지갑의 긴 쪽보다 크다면 지갑에 안 들어갑니다.
        # 둘 중 하나라도 참(True)이면 지폐를 더 접어야 하므로 while문 안으로 들어갑니다.

        if bill[0] > bill[1]:
            # bill[0]은 가로 길이, bill[1]은 세로 길이입니다.
            # 만약 가로(bill[0])가 세로(bill[1])보다 더 길다면 실행됩니다.
            
            bill[0] //= 2
            # 더 긴 쪽인 가로(bill[0])를 반으로 접습니다.
            # '//=' 연산자는 2로 나눈 몫(소수점 버림)을 구해서 다시 bill[0]에 저장하라는 뜻입니다.
            # 예: 26 //= 2 수행 시 bill[0]은 13이 됩니다.

        else:
            # 위의 if문 조건이 거짓일 때, 즉 세로(bill[1])가 가로(bill[0])보다 크거나 같을 때 실행됩니다.
            
            bill[1] //= 2
            # 더 긴 쪽(혹은 같은 쪽)인 세로(bill[1])를 반으로 접습니다.
            # 2로 나눈 몫을 구해서 다시 세로(bill[1]) 자리에 업데이트합니다.

        answer += 1
        # 가로든 세로든 지폐를 한 번 접었으므로, 접은 횟수(answer)에 1을 더해줍니다.
        # 이 줄이 끝나면 다시 맨 위 while문 조건식으로 올라가서 "이제 지갑에 들어가나?" 검사합니다.

    return answer
    # while문 조건이 거짓(False)이 되었다는 것은 지폐가 드디어 지갑에 쏙 들어간다는 뜻입니다.
    # 반복문을 탈출하고 지금까지 열심히 더해온 최종 접은 횟수(answer)를 정답으로 반환합니다.


