'''
문제 설명
지나다니는 길을 'O', 장애물을 'X'로 나타낸 직사각형 격자 모양의 공원에서 로봇 강아지가 산책을 하려합니다. 
산책은 로봇 강아지에 미리 입력된 명령에 따라 진행하며, 명령은 다음과 같은 형식으로 주어집니다.

["방향 거리", "방향 거리" … ]
예를 들어 "E 5"는 로봇 강아지가 현재 위치에서 동쪽으로 5칸 이동했다는 의미입니다. 
로봇 강아지는 명령을 수행하기 전에 다음 두 가지를 먼저 확인합니다.

주어진 방향으로 이동할 때 공원을 벗어나는지 확인합니다.
주어진 방향으로 이동 중 장애물을 만나는지 확인합니다.
위 두 가지중 어느 하나라도 해당된다면, 로봇 강아지는 해당 명령을 무시하고 다음 명령을 수행합니다.
공원의 가로 길이가 W, 세로 길이가 H라고 할 때, 공원의 좌측 상단의 좌표는 (0, 0), 우측 하단의 좌표는 (H - 1, W - 1) 입니다.
'''

'''
산책 : 미리 입력된 명령에 따라 진행
["방향거리","방향거리"]
    - ["E 5"]는 동쪽으로 5칸 이동
명령 수행전 확인할 2가지
    1) 주어진 방향으로 이동할때 공원을 벗어나는지
    2) 주어진 방향으로 이동 중 장애물을 만나는지
위 2가지 중 하나라도 해당한다면, 명령 무시 후, 다음 명령 수행


공원의 가로 길이 W, 세로 길이 H, 공원 좌측 상단 : (0,0), 우측 하단 : (H-1,W-1)

park : 공원을 나타내는 문자열 배열
routes : 로봇 강아지가 수행할 명령이 담긴 문자열 배열

출력 : 로봇 강아지가 모든 명령을 수행 후 놓인 위치 [세로방향좌표, 가로방향 좌표]순으로 배열에 담아 출력

step1) 시작 위치 (S)찾기
    - 공원(park)을 이중 반복문으로 돌면서 로봇 강아지의 초기위치(세로(r),가로(c))를 찾는다
step2) 방향 딕셔너리 정의하기
    - e,w,s,n으로 움직일때, 좌표가 각각 어떻게 변하는지 딕셔너리로 미리 정의해 둠
    - 북 :(-1,0), 남 :(1,0), 동 : (0,1), 서 : (0,-1)
step3) 명령(routes)을 하나씩 수행하며 검증
    - 각 명령에서 방향(op)과 이동할 칸 수 (n)을 분리
    - 현재 위치에서 한칸씩 이동해 보면서 아래 2조건 체크
        1) 공원의 범위를 벗어나는가? (0<=r<H,0<=c<W 인지)
        2) 이동할 칸에 장애물(x) 있는가?
    - 만약 중간에 한번이라도 조건을 만족하지 못하면(공원 벗어나거나 장애물 만남) 해당 명령 취소(무시)
    - 원래 위치에서 다음 명령
    - 아무 문제 없이 끝까지 이동했다면 로봇의 현재 위치 갱신
'''
def solution(park, routes):
    # 최종 결과(좌표)를 담아 반환할 리스트를 초기화합니다.
    answer = [] 
    
    # 공원의 세로 길이(행의 개수)를 구합니다.
    H = len(park)
    # 공원의 가로 길이(열의 개수, 첫 번째 행의 길이)를 구합니다.
    W = len(park[0])
    
    # 로봇 강아지의 현재 세로 위치(r)와 가로 위치(c)를 0으로 초기화합니다.
    r, c = 0, 0
    
    # 시작 위치 'S'를 찾기 위해 공원의 세로 크기만큼 반복합니다.
    for i in range(H):
        # 공원의 가로 크기만큼 반복합니다.
        for j in range(W):
            # 만약 현재 칸의 문자가 시작 지점인 'S'라면
            if park[i][j] == 'S':
                # 현재 위치 r과 c에 각각 i와 j 좌표를 저장합니다.
                r, c = i, j
                # 시작 위치를 찾았으므로 가로 반복문(안쪽 loop)을 탈출합니다.
                break
                
    # 각 방향(N, S, E, W)에 따른 [세로 이동량, 가로 이동량]을 딕셔너리로 정의합니다.
    move = {
        'N': (-1, 0),  # 북쪽: 세로로 -1칸 이동 (위로)
        'S': (1, 0),   # 남쪽: 세로로 +1칸 이동 (아래로)
        'E': (0, 1),   # 동쪽: 가로로 +1칸 이동 (오른쪽으로)
        'W': (0, -1)   # 서쪽: 가로로 -1칸 이동 (왼쪽으로)
    }
    
    # 이동 명령 배열(routes)에서 명령을 하나씩 꺼내어 반복합니다.
    for route in routes:
        # 명령 문자열을 공백 기준으로 나눕니다. (예: "E 2" -> op='E', n='2')
        op, n = route.split()
        # 문자열로 된 이동 칸 수(n)를 계산을 위해 정수형(int)으로 변환합니다.
        n = int(n)
        
        # move 딕셔너리에서 현재 방향(op)에 해당하는 세로(dr), 가로(dc) 이동량을 가져옵니다.
        dr, dc = move[op]
        # 명령을 수행하기 전, 현재 위치(r, c)를 임시 변수(temp_r, temp_c)에 복사해 둡니다.
        temp_r, temp_c = r, c
        # 이번 명령이 유효한지(이동 가능한지)를 나타내는 플래그 변수를 True로 설정합니다.
        is_valid = True
        
        # 지정된 칸 수(n)만큼 '한 칸씩' 이동하며 검사를 진행합니다.
        for _ in range(n):
            # 임시 세로 위치를 한 칸 이동시킵니다.
            temp_r += dr
            # 임시 가로 위치를 한 칸 이동시킵니다.
            temp_c += dc
            
            # 조건 1: 이동한 위치가 공원 범위를 벗어났는지 확인합니다.
            # 조건 2: 혹은 이동한 위치가 장애물('X')이 있는 칸인지 확인합니다.
            if not (0 <= temp_r < H and 0 <= temp_c < W) or park[temp_r][temp_c] == 'X':
                # 두 조건 중 하나라도 걸린다면 이동할 수 없으므로 플래그를 False로 바꿉니다.
                is_valid = False
                # 중간 경로에 문제가 생겼으므로, 더 전진할 필요 없이 한 칸씩 검사하던 루프를 중단합니다.
                break
                
        # n칸을 모두 이동하는 동안 장애물이나 공원 이탈이 없었다면 (is_valid가 True라면)
        if is_valid:
            # 로봇 강아지의 실제 위치(r, c)를 최종 이동된 임시 위치로 업데이트합니다.
            r, c = temp_r, temp_c
            
    # 모든 명령(routes) 처리가 끝난 후, 최종 위치 [세로, 가로]를 answer 변수에 담아줍니다.
    answer = [r, c]
    
    # 최종 위치가 담긴 answer 리스트를 반환합니다.
    return answer


# --- 테스트케이스 실행 및 검증 코드 ---

# 입출력 예 #1 (장애물 없고, 범위 안에서 무사히 이동하는 경우)
park1 = ["SOO", "OOO", "OOO"]
routes1 = ["E 2", "S 2", "W 1"]
print(f"테스트 1 결과: {solution(park1, routes1)} (기댓값: [2, 1])")


# 입출력 예 #2 (남쪽으로 이동 중 장애물 'X'를 만나서 해당 명령이 무시되는 경우)
park2 = ["SOO", "OXX", "OOO"]
routes2 = ["E 2", "S 2", "W 1"]
print(f"테스트 2 결과: {solution(park2, routes2)} (기댓값: [0, 1])")


# 입출력 예 #3 (첫 명령은 공원을 벗어나고, 두 번째 명령은 장애물을 지나쳐서 무시되는 경우)
park3 = ["OSO", "OOO", "OXO", "OOO"]
routes3 = ["E 2", "S 3", "W 1"]
print(f"테스트 3 결과: {solution(park3, routes3)} (기댓값: [0, 0])")


# --- 자동 검증 (틀린 결과가 나오면 에러를 발생시킵니다) ---
assert solution(park1, routes1) == [2, 1], "테스트 1번 실패!"
assert solution(park2, routes2) == [0, 1], "테스트 2번 실패!"
assert solution(park3, routes3) == [0, 0], "테스트 3번 실패!"

